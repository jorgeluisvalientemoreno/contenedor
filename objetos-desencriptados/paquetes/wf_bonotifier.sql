
CREATE OR REPLACE PACKAGE BODY WF_BONOTIFIER IS
   PROCEDURE RUN( INUEXECUTOR IN NUMBER, INUTHREAD IN NUMBER, SBPARAMETER IN VARCHAR2 )
    IS
    LANGUAGE JAVA NAME 'os.wf.comm.NotifyAgent.main_run(int, int, java.lang.String)';
   PROCEDURE NOTIFY_AGENT_RUN( INUPROCESS IN NUMBER, INUTHREAD IN NUMBER, ISBPARAMETERS IN VARCHAR2 )
    IS
    BEGIN
      RUN( INUPROCESS, INUTHREAD, ISBPARAMETERS );
    EXCEPTION
      WHEN EX.CONTROLLED_ERROR THEN
         RAISE EX.CONTROLLED_ERROR;
      WHEN OTHERS THEN
         ERRORS.SETERROR;
         RAISE EX.CONTROLLED_ERROR;
   END;
END WF_BONOTIFIER;
/


