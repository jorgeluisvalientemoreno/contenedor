            AND (CT_BOCERTIFICATECONTROL.TBGLOBALCERTIFDETAILS.COUNT > 0)) THEN
            NUPOS := 1;
            
            NUINDEX := CT_BOCERTIFICATECONTROL.TBGLOBALCERTIFDETAILS.FIRST;
