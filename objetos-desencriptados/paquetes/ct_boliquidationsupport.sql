                NUPOS := NUPOS + 1;
                NUINDEX := CT_BOCERTIFICATECONTROL.TBGLOBALCERTIFDETAILS.NEXT(NUINDEX);
            END LOOP;

            CT_BOCERTIFICATECONTROL.TBGLOBALCERTIFDETAILS := TBCERTIFDETAILS;
        END IF;
    EXCEPTION
        WHEN EX.CONTROLLED_ERROR THEN
            RAISE EX.CONTROLLED_ERROR;
