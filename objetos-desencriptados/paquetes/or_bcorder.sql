    	        CLOSE RFVALIDSTATE;
    	    END IF;

        OPEN RFVALIDSTATE FOR
            SELECT 1
                FROM OR_ORDER
                /*+ OR_BCORDER.fsbIsValidStateOrder SAO182730 */
