            MO_BOACTIONPARAMETER.FNUGETACTION_ANNUL_FINAL,
            RCCOMPONENT.PROD_MOTIVE_COMP_ID
            );

        
        MO_BOACTIONCONTROLLER.COMPONENTINTERNALTRANSITION(INUCOMPONENTID,MO_BOACTIONPARAMETER.FNUGETACTION_ANNUL_FINAL);

        
