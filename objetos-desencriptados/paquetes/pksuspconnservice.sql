                NUTOTALTHREADS,
                NUNUMETHREAD,
                CNUBULK,
                TBCONTRACTS
            );

            NUIDX := TBCONTRACTS.TBSUSCCODI.FIRST;

            
            LOOP
            
                
                EXIT WHEN NUIDX IS NULL;

                
                NUCURRSUBSCRIBER := TBCONTRACTS.TBSUSCCODI( NUIDX );

                
                NUCURRCYCLE := TBCONTRACTS.TBCYCLES( NUIDX );

                
                NUCURRSUSCCLIE := TBCONTRACTS.TBSUSCCLIE( NUIDX );

                
