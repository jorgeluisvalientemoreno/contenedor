    FUNCTION FBLPROCESSSERVSUSC
    RETURN BOOLEAN
    IS
        
        NUCUENSALDSERV	    PKBCSERVSUSC.STYSESUCUSA;
        
        RCCOMMRELATIONTYPE  DACC_RELATION_TYPE.STYCC_RELATION_TYPE;
        
        SBEST_FIN_CASTIGADO CONSTANT SERVSUSC.SESUESFN%TYPE := 'C';
