                                                 RCSERVSUSC.SESULOCA,
                                                 RCSERVSUSC.SESUNUSE,
                                                 CSBCONEXION )
        ) THEN
        
            UT_TRACE.TRACE( 'El producto tiene una orden de reconexin pendiente de atender, no genera orden', 7 );
            RETURN FALSE;
        
        END IF;
        
        
        IF   NVL (RCSERVSUSC.SESUESFN, PKCONSTANTE.NULLSB) = SBEST_FIN_CASTIGADO
        THEN
        
            UT_TRACE.TRACE( 'El producto est castigada, no genera orden', 6 );
            RETURN FALSE;
        
        END IF;

        
	    NUCUENSALDSERV := PKBOEXPIREDACCOUNTS.FNUPERIODSWITHEXPACCOUNTS( RCSERVSUSC.SESUNUSE );

        UT_TRACE.TRACE( 'Valida el nmero de perodos vencidos del producto, Perodos vencidos producto ['
                       || NUCUENSALDSERV || '] -> Perodos vencidos para reconexin [' || NUCUENSALD || ']', 7 );
                       
	    
        
	    IF ( NUCUENSALDSERV > NUCUENSALD ) THEN
	    
            UT_TRACE.TRACE( 'Nmero de perodos vencidos del producto es mayor al nmero de perodos para reconexin, no genera orden', 7 );
            RETURN FALSE;
        
        END IF;

    	
        IF ( RCSERVSUSC.SESUROGA = PKWARRANTYMGR.CSBPROPIETARIO) THEN
        
            UT_TRACE.TRACE( 'Producto es propietario, no genera orden ', 7 );
            RETURN FALSE;
        
    	END IF;
    	
        UT_TRACE.TRACE( 'Fin: [pkSuspConnService.ConnectionOrder.fblProcessServsusc]', 6 );
        RETURN TRUE;

