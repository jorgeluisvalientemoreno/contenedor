            IF ( NOT PR_BOSUSPCORTERECONEXION.FBOESVALIDOPARACONEXION( RCSERVSUSC.SESUNUSE ) ) THEN
            
                UT_TRACE.TRACE( 'Producto no es vlido para conexin, no genera orden', 7 );
                RETURN FALSE;
            
