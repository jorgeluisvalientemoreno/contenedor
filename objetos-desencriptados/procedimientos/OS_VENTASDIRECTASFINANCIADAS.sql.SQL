
CREATE OR REPLACE PROCEDURE OS_VENTASDIRECTASFINANCIADAS( INUPRODUCTO IN SERVSUSC.SESUNUSE%TYPE, INUPLANDIFE IN PLANDIFE.PLDICODI%TYPE, INUDIFENUCU IN DIFERIDO.DIFENUCU%TYPE, INUDIFENUDO IN DIFERIDO.DIFENUDO%TYPE, ITBCARGOS IN FI_BOVENTASDIRECTASFINANCIADAS.TYTBCARGOS, ISBDIFEPROG IN DIFERIDO.DIFEPROG%TYPE, ONUDIFECOFI OUT DIFERIDO.DIFECOFI%TYPE, ONUCODERROR OUT GE_ERROR_LOG.ERROR_LOG_ID%TYPE, OSBMENERROR OUT GE_ERROR_LOG.DESCRIPTION%TYPE )
 IS
 BEGIN
   PKERRORS.PUSH( 'OS_VentasDirectasFinanciadas' );
   FI_BSVENTASDIRECTASFINANCIADAS.REGISTRAR( INUPRODUCTO, INUPLANDIFE, INUDIFENUCU, INUDIFENUDO, ITBCARGOS, ISBDIFEPROG, ONUDIFECOFI, ONUCODERROR, OSBMENERROR );
   PKERRORS.POP;
 EXCEPTION
   WHEN LOGIN_DENIED OR PKCONSTANTE.EXERROR_LEVEL2 THEN
      PKERRORS.POP;
      PKERRORS.GETERRORVAR( ONUCODERROR, OSBMENERROR );
   WHEN OTHERS THEN
      PKERRORS.NOTIFYERROR( PKERRORS.FSBLASTOBJECT, SQLERRM, OSBMENERROR );
      PKERRORS.POP;
      PKERRORS.GETERRORVAR( ONUCODERROR, OSBMENERROR );
END OS_VENTASDIRECTASFINANCIADAS;
/


