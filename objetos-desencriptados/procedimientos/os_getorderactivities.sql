CREATE OR REPLACE PROCEDURE OS_GETORDERACTIVITIES( INUORDERID IN OR_ORDER.ORDER_ID%TYPE, ORFREADACTIVITIES OUT CONSTANTS.TYREFCURSOR, ONUERRORCODE OUT GE_ERROR_LOG.MESSAGE_ID%TYPE, OSBERRORMSG OUT GE_ERROR_LOG.DESCRIPTION%TYPE )
 IS
 BEGIN
   OR_BSORDERINFO.GETORDERACTIVITIES( INUORDERID, ORFREADACTIVITIES, ONUERRORCODE, OSBERRORMSG );
 EXCEPTION
   WHEN EX.CONTROLLED_ERROR THEN
      ERRORS.GETERROR( ONUERRORCODE, OSBERRORMSG );
   WHEN OTHERS THEN
      ERRORS.SETERROR;
      ERRORS.GETERROR( ONUERRORCODE, OSBERRORMSG );
END OS_GETORDERACTIVITIES;
/


