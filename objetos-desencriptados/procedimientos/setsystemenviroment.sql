CREATE OR REPLACE PROCEDURE SETSYSTEMENVIROMENT( SBPASSWORD IN VARCHAR2 := NULL, SBUSERMASK IN SA_USER.MASK%TYPE := NULL )
 AUTHID CURRENT_USER
 IS
 BEGIN
   IF ( SBUSERMASK IS NULL ) THEN
      SA_BOSYSTEM.SETSYSTEMENVIROMENT( SBPASSWORD, FALSE );
    ELSE
      SA_BOSYSTEM.SETSYSTEMENVIROMENT( SBPASSWORD, FALSE, SBUSERMASK );
   END IF;
END;
/


