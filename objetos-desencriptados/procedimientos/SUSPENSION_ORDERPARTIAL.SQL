
CREATE OR REPLACE PROCEDURE SUSPENSION_ORDERPARTIAL
 IS
   SBERRMSG VARCHAR2( 2000 );
   NUORDERTYPE NUMBER;
 BEGIN
   PKERRORS.PUSH( 'Suspension_OrderPartial' );
   PKGENERALSERVICES.TRACEDATA( ' Inicio de Procedimiento configurado Suspension_OrderPartial' );
   NUORDERTYPE := BI_BOCONSTANTS.CNUSUSPENSIONPARTIALVALUE;
   PKGENERALSERVICES.TRACEDATA( ' Fija Tipo de Orden OSS Suspension Parcial :' || NUORDERTYPE );
   PKSUSPCONNSERVICEMGR.SETORDERTYPE( NUORDERTYPE );
   SUSPENSION_ORDER;
   PKGENERALSERVICES.TRACEDATA( ' Fin de Procedimiento configurado Suspension_OrderPartial' );
   PKERRORS.POP;
 EXCEPTION
   WHEN LOGIN_DENIED THEN
      PKERRORS.POP;
      RAISE LOGIN_DENIED;
   WHEN PKCONSTANTE.EXERROR_LEVEL2 THEN
      PKERRORS.POP;
      RAISE PKCONSTANTE.EXERROR_LEVEL2;
   WHEN OTHERS THEN
      PKERRORS.NOTIFYERROR( PKERRORS.FSBLASTOBJECT, SQLERRM, SBERRMSG );
      PKERRORS.POP;
      RAISE_APPLICATION_ERROR( PKCONSTANTE.NUERROR_LEVEL2, SBERRMSG );
END SUSPENSION_ORDERPARTIAL;
/


