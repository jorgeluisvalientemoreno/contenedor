CREATE OR REPLACE PROCEDURE OS_CHANGE_TASKTYPE( ISBTASKTYPECHG IN GE_BOUTILITIES.STYSTATEMENT, ONUERRORCODE OUT GE_ERROR_LOG.ERROR_LOG_ID%TYPE, OSBERRORMESSAGE OUT GE_ERROR_LOG.DESCRIPTION%TYPE )
 IS
 BEGIN
   OR_BSORDERACTIVITIES.CHANGETASKTYPE( ISBTASKTYPECHG, ONUERRORCODE, OSBERRORMESSAGE );
 EXCEPTION
   WHEN EX.CONTROLLED_ERROR THEN
      ERRORS.GETERROR( ONUERRORCODE, OSBERRORMESSAGE );
   WHEN OTHERS THEN
      ERRORS.SETERROR;
      ERRORS.GETERROR( ONUERRORCODE, OSBERRORMESSAGE );
END;
/


