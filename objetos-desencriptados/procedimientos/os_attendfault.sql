
CREATE OR REPLACE PROCEDURE OS_ATTENDFAULT( INUFAULTID IN TT_DAMAGE.PACKAGE_ID%TYPE, INUFAULTTYPEID IN TT_DAMAGE.FINAL_DAMAGE_TYPE_ID%TYPE, INUCLOSURECAUSEID IN TT_DAMAGE.DAMAGE_CAUSAL_ID%TYPE, IDTINITIALDATE IN TT_DAMAGE.INITIAL_DATE%TYPE, IDTENDDATE IN TT_DAMAGE.END_DATE%TYPE, ISBAFFECTEDELEMS IN VARCHAR2, ONUERRORCODE OUT GE_ERROR_LOG.MESSAGE_ID%TYPE, OSBERRORMESSAGE OUT GE_ERROR_LOG.DESCRIPTION%TYPE )
 IS
 BEGIN
   TT_BSFAULT.ATTENDFAULT( INUFAULTID, INUFAULTTYPEID, INUCLOSURECAUSEID, IDTINITIALDATE, IDTENDDATE, ISBAFFECTEDELEMS, ONUERRORCODE, OSBERRORMESSAGE );
 EXCEPTION
   WHEN EX.CONTROLLED_ERROR THEN
      ERRORS.GETERROR( ONUERRORCODE, OSBERRORMESSAGE );
   WHEN OTHERS THEN
      ERRORS.SETERROR;
      ERRORS.GETERROR( ONUERRORCODE, OSBERRORMESSAGE );
END OS_ATTENDFAULT;
/


