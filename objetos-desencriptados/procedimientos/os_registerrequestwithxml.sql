CREATE OR REPLACE PROCEDURE OS_REGISTERREQUESTWITHXML( ISBREQUESTXML IN VARCHAR2, ONUPACKAGEID OUT MO_PACKAGES.PACKAGE_ID%TYPE, ONUMOTIVEID OUT MO_MOTIVE.MOTIVE_ID%TYPE, ONUERRORCODE OUT GE_MESSAGE.MESSAGE_ID%TYPE, OSBERRORMESSAGE OUT VARCHAR2 )
 IS
 BEGIN
   MO_BSREGISTERWITHXML.REGISTERREQUESTBYXML( ISBREQUESTXML, ONUPACKAGEID, ONUMOTIVEID, ONUERRORCODE, OSBERRORMESSAGE );
 EXCEPTION
   WHEN EX.CONTROLLED_ERROR THEN
      ERRORS.GETERROR( ONUERRORCODE, OSBERRORMESSAGE );
   WHEN OTHERS THEN
      ERRORS.SETERROR;
      ERRORS.GETERROR( ONUERRORCODE, OSBERRORMESSAGE );
END OS_REGISTERREQUESTWITHXML;
/


