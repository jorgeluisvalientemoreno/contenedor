SELECT *
FROM OPEN.PR_PROD_SUSPENSION
WHERE PRODUCT_ID=17149565;

SELECT *
FROM OPEN.PR_COMPONENT 
WHERE PRODUCT_ID=17149565;

SELECT *
FROM COMPSESU C
WHERE C.CMSSSESU=17149565;

SELECT *
FROM OPEN.PR_COMP_SUSPENSION S
WHERE S.COMPONENT_ID IN (SELECT C.COMPONENT_ID FROM OPEN.PR_COMPONENT C WHERE C.PRODUCT_ID=17149565);

SELECT *
FROM OPEN.MO_MOTIVE M, OPEN.MO_SUSPENSION S
WHERE M.PRODUCT_ID=17149565
  AND M.MOTIVE_ID=S.MOTIVE_ID
  AND S.SUSPENSION_TYPE_ID=101
;

SELECT *
FROM OPEN.MO_MOTIVE M, OPEN.MO_COMPONENT MC, MO_SUSPENSION_COMP MCC
WHERE M.PRODUCT_ID=17149565
  AND M.MOTIVE_ID=MC.MOTIVE_ID
  AND MC.COMPONENT_ID=MCC.COMPONENT_ID
  AND MCC.SUSPENSION_TYPE_ID=101
;
