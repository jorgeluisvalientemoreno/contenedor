SELECT A.COMMERCIAL_PLAN_ID ID, A.DESCRIPTION
FROM open.CC_COMMERCIAL_PLAN A, OPEN.LDC_TIPSOLPLANCOMERCIAL B
WHERE A.INITIAL_DATE <= SYSDATE AND
A.FINAL_DATE >= SYSDATE AND
A.PACKAGE_TYPE_ID = 587 AND
A.PRODUCT_TYPE_ID = 7014 AND
A.COMMERCIAL_PLAN_ID = B.COMMERCIAL_PLAN_ID AND
B.PACKAGE_TYPE_ID = 271 