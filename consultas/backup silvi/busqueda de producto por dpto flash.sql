SELECT  P.SUBSCRIPTION_ID ,ge.geograp_location_id , P.PRODUCT_ID,  GE.DISPLAY_DESCRIPTION                                                                      
FROM AB_ADDRESS A 
INNER JOIN  PR_PRODUCT P  ON  P.ADDRESS_ID = A.ADDRESS_ID
INNER JOIN GE_GEOGRA_LOCATION GE ON GE.GEOGRAP_LOCATION_ID = A.GEOGRAP_LOCATION_ID
WHERE GE.DISPLAY_DESCRIPTION LIKE '%ATLANTICO%' 
GROUP BY ( P.SUBSCRIPTION_ID , P.PRODUCT_ID,  GE.DISPLAY_DESCRIPTION ,ge.geograp_location_id  ) ;

