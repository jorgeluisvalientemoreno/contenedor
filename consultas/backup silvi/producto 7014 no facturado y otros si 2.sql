WITH CONTRATOS_FACT AS ( 
SELECT S1.SESUSUSC CONTRATOS ,B1.CUCOFACT FACTU  FROM CARGOS C1
LEFT JOIN SERVSUSC S1 ON S1.SESUNUSE = C1.CARGNUSE AND C1.CARGPEFA IN (105615)
LEFT JOIN CUENCOBR B1 ON B1.CUCOCODI = C1.CARGCUCO  
WHERE C1.CARGCUCO >1 AND C1.CARGCACA IN (15,51) AND S1.SESUSERV=7014
),
OTROS_PROD AS (
SELECT S2.sesunuse  PRODUCTOS , S2.SESUSUSC CONTR
FROM SERVSUSC S2
WHERE s2.SESUCICL  IN (701)
AND S2.SESUESFN <>'C'
AND S2.SESUESCO NOT IN (92,94)
AND S2.SESUSUSC NOT IN ( SELECT CONTRATOS FROM CONTRATOS_FACT) AND SESUSERV IN (7055,7053)
AND (SELECT  (SUM(BR2.CUCOSACU) - SUM(BR2.CUCOVARE) - SUM(BR2.CUCOVRAP))
     FROM SERVSUSC S3, CUENCOBR BR2
     WHERE  BR2.CUCONUSE = S3.SESUNUSE
     AND S3.SESUNUSE = S2.SESUNUSE
     AND BR2.CUCOSACU > 0
     AND BR2.CUCOFACT NOT IN (SELECT FACTU FROM CONTRATOS_FACT  )
     GROUP BY S3.SESUNUSE, S3.SESUSERV) >0
AND (SELECT count ( distinct (cucocodi))
     FROM SERVSUSC S4, CUENCOBR BR3
     WHERE  BR3.CUCONUSE = S4.SESUNUSE
     AND S4.SESUNUSE = S2.SESUNUSE
     AND BR3.CUCOSACU > 0
     AND BR3.CUCOFACT NOT IN (SELECT FACTU FROM CONTRATOS_FACT  )
     GROUP BY S4.SESUNUSE, S4.SESUSERV) <= 3
)
SELECT * FROM OTROS_PROD
where rownum<=10
