SELECT *
FROM LD_POLICY L
INNER JOIN CONTRATO ON SUSCRIPTION_ID = CONTRATO
WHERE PRODUCT_LINE_ID = 131
AND EMPRESA= 'GDCA'
AND STATE_POLICY = 1
AND TO_NUMBER(SUBSTR(COLLECTIVE_NUMBER, 3, 4)) = 03
AND NOT EXISTS (SELECT  'X'
FROM LD_SECURE_CANCELLA C, MO_PACKAGES S
WHERE L.POLICY_ID = C.POLICY_ID
AND  C.SECURE_CANCELLA_ID = S.PACKAGE_ID
AND S.MOTIVE_STATUS_ID = 13)
