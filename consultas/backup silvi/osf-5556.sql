SELECT SUSCCLIE, SUSCCODI , SUSCTISU, SUSCCICL , SESUMECV , CONTRATO.EMPRESA EMPRESA_CONTRATO  ,CICLO_FACTURACION.EMPRESA EMPRESA_CICLO 
FROM SUSCRIPC 
INNER JOIN CONTRATO ON CONTRATO=SUSCCODI 
INNER JOIN SERVSUSC ON SESUSUSC=SUSCCODI 
INNER JOIN CICLO_FACTURACION ON CICLO= SESUCICL 
WHERE SUSCCLIE  = 3529036 
GROUP BY SUSCCLIE, SUSCCODI , SUSCTISU, SUSCCICL , SESUMECV , CONTRATO.EMPRESA   ,CICLO_FACTURACION.EMPRESA  ;

