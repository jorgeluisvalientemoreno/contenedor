WITH CONTRATOS_GDC AS (
SELECT SESUSUSC , SESUNUSE , SESUSERV, SESUESCO, SESUESFN 
FROM SERVSUSC
INNER JOIN CONTRATO ON SESUSUSC = CONTRATO
WHERE EMPRESA = 'GDCA'
and sesuesco =1)
,
CUENTAS_CONSALDO AS (
SELECT SESUSUSC , SESUNUSE , SESUSERV, SESUESCO, SESUESFN ,COUNT(DISTINCT CUCOCODI) NUM_CUENTAS_PEND
FROM CUENCOBR C 
INNER JOIN CONTRATOS_GDC G ON CUCONUSE = SESUNUSE
WHERE CUCOSACU >0 
GROUP BY SESUSUSC , SESUNUSE , SESUSERV, SESUESCO, SESUESFN
HAVING COUNT (DISTINCT CUCOCODI) >=3),

DIFERIDOS_GCNED AS (

SELECT SESUSUSC , SESUNUSE , SESUSERV, SESUESCO, SESUESFN , NUM_CUENTAS_PEND , COUNT(DISTINCT DIFECODI) NUM_DIFERIDOS_GCNED
FROM DIFERIDO 
INNER JOIN CUENTAS_CONSALDO ON DIFENUSE = SESUNUSE AND SESUSUSC = DIFESUSC 
WHERE DIFESAPE>0 
AND DIFEPROG='GCNED' 
GROUP BY SESUSUSC , SESUNUSE , SESUSERV, SESUESCO, SESUESFN , NUM_CUENTAS_PEND)

SELECT *
FROM DIFERIDOS_GCNED
WHERE NUM_CUENTAS_PEND>3 
AND NUM_DIFERIDOS_GCNED >1 
AND ROWNUM <=10
