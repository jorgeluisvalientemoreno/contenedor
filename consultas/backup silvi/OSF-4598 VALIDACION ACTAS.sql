WITH BASE AS
(SELECT G.ID_ACTA , SUM( VALOR_TOTAL) AS VALOR_BRUTO   , ROUND ( SUM( VALOR_TOTAL) * 0.02 , 3)  IMPUESTO
 FROM GE_DETALLE_ACTA G
 INNER JOIN GE_ITEMS I ON I.ITEMS_ID = G.ID_ITEMS
 WHERE ID_ACTA = 250613
 AND ITEM_CLASSIF_ID != 23
 GROUP BY G.ID_ACTA  ) ,

 ESTAMPILLAS_NUEVAS AS (
 SELECT G.ID_ITEMS ITEMS_ESTAMPILLAS, G.ID_ACTA, VALOR_TOTAL
 FROM GE_DETALLE_ACTA G
 WHERE ID_ACTA = 250613
 AND ID_ITEMS IN (100010613,100010614 ))

 SELECT VALOR_BRUTO ,ITEMS_ESTAMPILLAS , E.VALOR_TOTAL "ESTAMPILLAS NUEVAS" , IMPUESTO IMPUESTO_CALCULADO , IMPUESTO + E.VALOR_TOTAL DIFERENCIA
 FROM ESTAMPILLAS_NUEVAS E
 INNER JOIN BASE B  ON E.ID_ACTA = B.ID_ACTA
