   SELECT
  0 ID_CONTRATO --SECUENCIA DE CONTRATO
  ,CT.CONTDENU  CONTDENU --USADO PARA CU DESC
  ,CT.CONTLONU  CONTLONU --USADO PARA CU DESC
  ,CT.CONTNUME  CONTNUME --USADO PARA CU DESC
  ,CT.CONTCUAD  CONTCUAD --USADA PARA CU IDCTSTA
  ,CT.CONTCLAS  CONTCLAS --USADA PARA HOMOLOGACION
  ,CT.CONTFEIN FECHA_INICIAL
  ,CT.CONTFETE FECHA_FINAL
  ,NULL VALOR_AUI_ADMIN
  ,NULL VALOR_AUI_UTIL
  ,NULL VALOR_AUI_IMPREV
  ,'PC' TIPO_MONEDA_LIQUIDAR
  ,CT.CONTVACO VALOR_TOTAL_CONTRATO
  --,0 ID_TIPO_CONTRATO --HOMOLOGACION CLASES DE CONTRATO
  ,NULL  DIAS_FACTURAR
  ,80 ALERTA_PORCEN_VALOR
  ,0 VALOR_ANTICIPO
  ,0 ANTICIPO_AMORTIZADO
  ,NULL FECHA_CIERRE
  ,'AB' STATUS
  ,NULL PERCENT_NATION_STAMP
  ,NULL ACCOUNT_CLASSIF_ID
  ,0 VALOR_ASIGNADO
  ,0 VALOR_NO_LIQUIDADO
    ,'N'  FLAG_LOAD --21/10/2025 10:15:09
  FROM GASGG.CONTRATO CT
  WHERE CONTESCO in (4,5)
    AND CT.CONTCUAD NOT IN (    SELECT TO_NUMBER(TRIM(REGEXP_SUBSTR(VALOR, '[^,]+', 1, LEVEL)))
                                FROM HOMOLOGACION.migr_exclusiones
                                WHERE PROCESO = 'PRDMIGR_GE_CONTRATO'
                                AND VALOR IS NOT NULL
                                CONNECT BY REGEXP_SUBSTR(VALOR, '[^,]+', 1, LEVEL) IS NOT NULL
                                AND PRIOR PROCESO = PROCESO
                                AND PRIOR SYS_GUID() IS NOT NULL)
