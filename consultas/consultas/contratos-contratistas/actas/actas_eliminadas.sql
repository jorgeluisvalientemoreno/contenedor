SELECT  xml_log,l.current_date, l.current_user_mask, tabla_ante.*
FROM OPEN.AU_AUDIT_POLICY_LOG_HISTORY l, xmltable('AU_LOG' passing xml_log
columns id_acta number path '//PREVIOUS_VALUES//MODIFICACIONES//ID_ACTA',
nombre varchar2(4000) path '//PREVIOUS_VALUES//MODIFICACIONES//NOMBRE',
ID_TIPO_ACTA varchar2(4000) path '//PREVIOUS_VALUES//MODIFICACIONES//ID_TIPO_ACTA',
ESTADO varchar2(4000) path '//PREVIOUS_VALUES//MODIFICACIONES//ESTADO',
ID_CONTRATO varchar2(4000) path '//PREVIOUS_VALUES//MODIFICACIONES//ID_CONTRATO',
EXTERN_PAY_DATE varchar2(4000) path '//PREVIOUS_VALUES//MODIFICACIONES//EXTERN_PAY_DATE',
EXTERN_INVOICE_NUM varchar2(4000) path '//PREVIOUS_VALUES//MODIFICACIONES//EXTERN_INVOICE_NUM') tabla_ante
where AUDIT_LOG_ID IN (206)
and current_even_desc='DELETE'
and tabla_ante.estado='C'
;


