 
SELECT CUENTA, (SELECT DESCRIPTION FROM OPEN.OR_TASK_TYPE T WHERE T.TASK_TYPE_ID=C.TITR), C.ACTIVIDAD, SUM(COSTO)
FROM OPEN.LDC_OSF_COSTINGR C, OPEN.OR_ORDER O
WHERE NUANO=2019
  AND NUMES=2
  AND CUENTA IN ('7530040000','7570900100','7570900600')
  --AND C.TIPO IN ('ACTA_S_F','SIN_ACTA')
  AND O.ORDER_ID=C.ORDER_ID
  AND O.ORDER_STATUS_ID=12
  GROUP BY CUENTA, C.TITR, C.ACTIVIDAD;
