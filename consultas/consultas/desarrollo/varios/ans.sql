SELECT ANS_ID, DESCRIPCION, PACKAGE_TYPE_ID, OPEN.DAPS_PACKAGE_TYPE.FSBGETDESCRIPTION(PACKAGE_TYPE_ID)
FROM --OPEN.MO_PACKAGES
OPEN.CC_ASIG_ANS CA, OPEN.CC_CRITERIOS_ASIG CC, OPEN.CC_ASIG_PACK_TYPE PC
WHERE CC.criterios_asig_id =CA.criterios_asig_id 
  AND CC.criterios_asig_id=PC.CRITERIOS_ASIG_ID;
SELECT *
FROM OPEN.CC_TIPO_ASIG;
select *
from open.ps_package_type
where upper(description) like '%VENTA%SEGURO%'
select *
from OPEN.PS_PACK_TYPE_PARAM
where package_type_id=100236;
SELECT *
FROM OPEN.CC_ASIG_PACK_TYPE
WHERE PACKAGE_TYPE_ID=100236;

SELECT *
FROM OPEN.or_task_type
WHERE PACKAGE_TYPE_ID=100236;

SELECT *
FROM OPEN.OR_TASK_TYPES_ITEMS IT, OPEN.GE_ITEMS I
WHERE TASK_TYPE_ID=12150
  AND IT.ITEMS_ID=I.ITEMS_ID
  AND I.ITEM_CLASSIF_ID=2;
