
CREATE OR REPLACE PROCEDURE ATTENTCONNECTION
 IS
   SBERRMSG VARCHAR2( 2000 );
 BEGIN
   PKERRORS.PUSH( 'AttentConnection' );
   BI_BSSUSPCONNWITHDRAW.ATTENTSUSPCONNSERV;
   PKERRORS.POP;
 EXCEPTION
   WHEN LOGIN_DENIED THEN
      PKERRORS.POP;
      RAISE LOGIN_DENIED;
   WHEN PKCONSTANTE.EXERROR_LEVEL2 THEN
      PKERRORS.POP;
      RAISE PKCONSTANTE.EXERROR_LEVEL2;
   WHEN OTHERS THEN
      PKERRORS.NOTIFYERROR( PKERRORS.FSBLASTOBJECT, SQLERRM, SBERRMSG );
      PKERRORS.POP;
      RAISE_APPLICATION_ERROR( PKCONSTANTE.NUERROR_LEVEL2, SBERRMSG );
END ATTENTCONNECTION;
/


