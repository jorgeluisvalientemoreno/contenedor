
CREATE OR REPLACE PROCEDURE OS_UPDINFOPREMISE( INUPREMISEID IN AB_PREMISE.PREMISE_ID%TYPE, ISBRING IN AB_INFO_PREMISE.IS_RING%TYPE, IDTDATERING IN AB_INFO_PREMISE.DATE_RING%TYPE, ISBCONNECTION IN AB_INFO_PREMISE.IS_CONNECTION%TYPE, ISBINTERNAL IN AB_INFO_PREMISE.IS_INTERNAL%TYPE, INUINTERNALTYPE IN AB_INFO_PREMISE.INTERNAL_TYPE%TYPE, ISBMEAUSERMENT IN AB_INFO_PREMISE.IS_MEASUREMENT%TYPE, INUNUMPOINTS IN AB_INFO_PREMISE.NUMBER_POINTS%TYPE, INULEVELRISK IN AB_INFO_PREMISE.LEVEL_RISK%TYPE, ISBDESCRISK IN AB_INFO_PREMISE.DESCRIPTION_RISK%TYPE, ONUERRORCODE OUT GE_ERROR_LOG.ERROR_LOG_ID%TYPE, OSBERRORMESSAGE OUT GE_ERROR_LOG.DESCRIPTION%TYPE )
 IS
 BEGIN
   AB_BSPREMISE.UPDINFOPREMISE( INUPREMISEID, ISBRING, IDTDATERING, ISBCONNECTION, ISBINTERNAL, INUINTERNALTYPE, ISBMEAUSERMENT, INUNUMPOINTS, INULEVELRISK, ISBDESCRISK, ONUERRORCODE, OSBERRORMESSAGE );
 EXCEPTION
   WHEN EX.CONTROLLED_ERROR THEN
      ERRORS.GETERROR( ONUERRORCODE, OSBERRORMESSAGE );
   WHEN OTHERS THEN
      ERRORS.SETERROR;
      ERRORS.GETERROR( ONUERRORCODE, OSBERRORMESSAGE );
END OS_UPDINFOPREMISE;
/


