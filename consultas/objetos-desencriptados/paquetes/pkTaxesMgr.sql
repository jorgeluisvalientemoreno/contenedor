	PKGENERALSERVICES.TRACEDATA(TBCONCIMPCUENTA(NUINDICE).NUCONCEPTO);
	PKGENERALSERVICES.TRACEDATA(TBCONCIMPCUENTA(NUINDICE).NUVALOR);
