            PKCONCEPTMGR.GETRECORD
            (
                TBRCTAXCONCEPTS( NUTAXCONCIDX ).NUCONCEPTO,
                RCCONCEPTO
            );

            
            PKBCPERICOSE.GETCACHECONSPERBYBILLPER
            (
                IRCPRODUCT.SESUCICO,
                RCBILLINGPERIOD.PEFACODI,
                NUPERICOSE,
                RCSERVICIO.SERVTICO,
                RCCONCEPTO.CONCTICC
            );

            
