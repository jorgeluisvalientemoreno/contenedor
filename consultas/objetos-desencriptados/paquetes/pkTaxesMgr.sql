        EXIT WHEN NUTAXCONCIDX IS NULL;

        UT_TRACE.TRACE('Concepto: '||TBRCTAXCONCEPTS( NUTAXCONCIDX ).NUCONCEPTO, 5);

        IF ( TBNUTAXCONCLIQ.EXISTS( TBRCTAXCONCEPTS( NUTAXCONCIDX ).NUCONCEPTO ) ) THEN
        
            
