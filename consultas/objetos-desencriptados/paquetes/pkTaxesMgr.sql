    CURSOR CUTAXBASECONCEPTS 
    IS
	SELECT --+ INDEX (CONCEPTO PK_CONCEPTO)
           COBLCONC,COBLCOBA
	FROM   CONCBALI, CONCEPTO
	WHERE  CONCTICL = PKBILLCONST.FNUOBTTIPOIMP
	AND    COBLCONC =CONCCODI
	ORDER BY COBLCONC;
    
    
    
    
