        PKINSTANCEDATAMGR.GETRECORDBILLINGPERIOD( INUBILLINGPERIOD, RCBILLINGPERIOD );
    
    ELSE
    
        
        PKINSTANCEDATAMGR.GETRECORDBILLINGPERIODCURRENT( RCSUBSCRIPTION.SUSCCICL, RCBILLINGPERIOD );
    
    END IF;

    
