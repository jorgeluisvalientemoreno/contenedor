CREATE OR REPLACE PACKAGE BODY GE_BOERRORS IS
   PROCEDURE SETERROR
    IS
    BEGIN
      ERRORS.SETERROR;
      RAISE EX.CONTROLLED_ERROR;
   END;
   PROCEDURE SETERRORCODE( INUAPPERRORCODE IN GE_ERROR_LOG.ERROR_LOG_ID%TYPE )
    IS
    BEGIN
      ERRORS.SETERROR( INUAPPERRORCODE );
      RAISE EX.CONTROLLED_ERROR;
   END;
   PROCEDURE SETERRORCODEARGUMENT( INUAPPERRORCODE IN GE_ERROR_LOG.ERROR_LOG_ID%TYPE, ISBARGUMENT IN VARCHAR2 )
    IS
    BEGIN
      ERRORS.SETERROR( INUAPPERRORCODE, ISBARGUMENT );
      RAISE EX.CONTROLLED_ERROR;
   END;
   PROCEDURE EXCEPTIONCONTROLLED
    IS
    BEGIN
      RAISE EX.CONTROLLED_ERROR;
   END;
END GE_BOERRORS;
/


