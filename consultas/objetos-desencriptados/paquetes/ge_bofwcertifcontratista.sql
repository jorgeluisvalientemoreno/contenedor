        IF DTFECHAACTUALIZA_INI != TRUNC(UT_DATE.FDTMINDATE) THEN
            SBSQL := SBSQL ||' AND trunc(GE_ACTA.fecha_ult_actualizac) >= '|| CHR(39)|| DTFECHAACTUALIZA_INI ||CHR(39)|| CHR(10);
        END IF;

        
        IF DTFECHAACTUALIZA_FIN != TRUNC(UT_DATE.FDTMAXDATE) THEN
