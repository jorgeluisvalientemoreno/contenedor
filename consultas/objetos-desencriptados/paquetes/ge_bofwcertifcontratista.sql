        OPEN OCUDATACURSOR FOR SBSQL USING CC_BOBOSSUTIL.CNUNULL;

        UT_TRACE.TRACE('FIN Ge_BofwCertifContratista.obtActas',5);

    EXCEPTION
        WHEN EX.CONTROLLED_ERROR THEN
