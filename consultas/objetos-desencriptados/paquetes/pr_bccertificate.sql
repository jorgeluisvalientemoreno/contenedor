PACKAGE BODY PR_BCCertificate IS

















	
    CSBVERSION              CONSTANT VARCHAR2(20) := 'SAO195690';

    
    FUNCTION FSBVERSION  RETURN VARCHAR2 IS
    BEGIN
        RETURN CSBVERSION;
    END;
    
    














    FUNCTION FBLISCERTIFIEDPRODUCT(INUPRODUCTID    IN  PR_PRODUCT.PRODUCT_ID%TYPE)
    RETURN BOOLEAN
    IS

    BEGIN
        UT_TRACE.TRACE('Inicia OR_BCReview.fblIsCertifiedProduct',15);

        FOR RCROW IN CUGETPRODUCTCERTIFICATE(INUPRODUCTID, SYSDATE) LOOP
            RETURN TRUE;
        END LOOP;

        UT_TRACE.TRACE('Finaliza OR_BCReview.fblIsCertifiedProduct',15);
        RETURN FALSE;
        
    EXCEPTION
        WHEN EX.CONTROLLED_ERROR THEN
            UT_TRACE.TRACE('Error : ex.CONTROLLED_ERROR',15);
            RAISE;

        WHEN OTHERS THEN
            UT_TRACE.TRACE('Error : others',15);
            ERRORS.SETERROR;
            RAISE EX.CONTROLLED_ERROR;
    END;

END PR_BCCERTIFICATE;