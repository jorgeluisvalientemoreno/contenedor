    IS
        NUINSTANCE          GE_BOINSTANCECONTROL.STYNUINDEX;
        NUENTITYINDEX       GE_BOINSTANCECONTROL.STYNUINDEX;
        CSBFILE_ATTRIBUTE   GE_BOINSTANCECONTROL.STYSBNAME := 'FILE_ID';

