PACKAGE BODY dbms_application_info IS



  PROCEDURE ICD_SET_MODULE(MODULE_NAME VARCHAR2, ACTION_NAME VARCHAR2);
    PRAGMA INTERFACE (C, ICD_SET_MODULE);               

  PROCEDURE ICD_SET_ACTION(ACTION_NAME VARCHAR2);
    PRAGMA INTERFACE (C, ICD_SET_ACTION);               

  PROCEDURE ICD_READ_MODULE(MODULE_NAME OUT VARCHAR2);
    PRAGMA INTERFACE (C, ICD_READ_MODULE);              

  PROCEDURE ICD_READ_ACTION(ACTION_NAME OUT VARCHAR2);
    PRAGMA INTERFACE (C, ICD_READ_ACTION);              

  PROCEDURE ICD_SET_CLIENT_INFO(CLIENT_INFO VARCHAR2);
    PRAGMA INTERFACE (C, ICD_SET_CLIENT_INFO); 	        

  PROCEDURE ICD_READ_INFO(CLIENT_INFO OUT VARCHAR2);
    PRAGMA INTERFACE (C, ICD_READ_INFO);                

  PROCEDURE ICD_SET_SESSION_LONGOPS(RINDEX      IN OUT PLS_INTEGER,
                                    SLNO        IN OUT PLS_INTEGER,
                                    OP_NAME     IN VARCHAR2 DEFAULT NULL,
                                    TARGET      IN PLS_INTEGER DEFAULT 0,
                                    CONTEXT     IN PLS_INTEGER DEFAULT 0,
                                    SOFAR       IN NUMBER DEFAULT 0,
                                    TOTALWORK   IN NUMBER DEFAULT 0,
                                    TARGET_DESC IN VARCHAR2
                                                   DEFAULT 'unknown target',
                                    UNITS       IN VARCHAR2 DEFAULT NULL);
    PRAGMA INTERFACE (C, ICD_SET_SESSION_LONGOPS);      


  PROCEDURE SET_MODULE(MODULE_NAME VARCHAR2, ACTION_NAME VARCHAR2) IS
  BEGIN 
    ICD_SET_MODULE(REPLACE(REPLACE(MODULE_NAME, CHR(10)), CHR(13)), 
                   REPLACE(REPLACE(ACTION_NAME, CHR(10)), CHR(13))); 
  END;
  
  PROCEDURE SET_ACTION(ACTION_NAME VARCHAR2) IS
  BEGIN 
    ICD_SET_ACTION(REPLACE(REPLACE(ACTION_NAME, CHR(10)), CHR(13))); 
  END;

  
  
  
  PROCEDURE READ_MODULE(MODULE_NAME OUT VARCHAR2, ACTION_NAME OUT VARCHAR2) IS
  BEGIN 
    ICD_READ_MODULE(MODULE_NAME);
    ICD_READ_ACTION(ACTION_NAME);
  END;

  PROCEDURE SET_CLIENT_INFO(CLIENT_INFO VARCHAR2) IS
  BEGIN 
    ICD_SET_CLIENT_INFO(REPLACE(REPLACE(CLIENT_INFO, CHR(10)), CHR(13))); 
  END;

  PROCEDURE READ_CLIENT_INFO(CLIENT_INFO OUT VARCHAR2) IS
    BEGIN ICD_READ_INFO(CLIENT_INFO); END;

  PROCEDURE SET_SESSION_LONGOPS(RINDEX      IN OUT PLS_INTEGER,
                                SLNO        IN OUT PLS_INTEGER,
                                OP_NAME     IN VARCHAR2 DEFAULT NULL,
                                TARGET      IN PLS_INTEGER DEFAULT 0,
                                CONTEXT     IN PLS_INTEGER DEFAULT 0,
                                SOFAR       IN NUMBER DEFAULT 0,
                                TOTALWORK   IN NUMBER DEFAULT 0,
                                TARGET_DESC IN VARCHAR2
                                               DEFAULT 'unknown target',
                                UNITS       IN VARCHAR2 DEFAULT NULL) IS
  BEGIN
    ICD_SET_SESSION_LONGOPS(RINDEX, SLNO, OP_NAME, TARGET, CONTEXT,
                            SOFAR, TOTALWORK, TARGET_DESC, UNITS);
  END SET_SESSION_LONGOPS;    

END;