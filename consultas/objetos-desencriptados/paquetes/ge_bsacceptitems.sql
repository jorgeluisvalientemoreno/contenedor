CREATE OR REPLACE PACKAGE BODY GE_BSACCEPTITEMS IS
   CSBVERSION CONSTANT VARCHAR2( 10 ) := 'SAO189710';
   PROCEDURE ACCEPTITEM( ICLXMLACCEPTITEMS IN CLOB, ONUERRORCODE OUT GE_ERROR_LOG.ERROR_LOG_ID%TYPE, OSBERRORMESSAGE OUT GE_ERROR_LOG.DESCRIPTION%TYPE )
    IS
    BEGIN
      UT_TRACE.TRACE( 'Inicio - GE_BSAcceptItems.AcceptItem', 1 );
      GE_BOUTILITIES.INITIALIZEOUTPUT( ONUERRORCODE, OSBERRORMESSAGE );
      GE_BOACEPTARITEMS.ACCEPTITEM( ICLXMLACCEPTITEMS );
      UT_TRACE.TRACE( 'Fin - GE_BSAcceptItems.AcceptItem', 1 );
    EXCEPTION
      WHEN EX.CONTROLLED_ERROR THEN
         ERRORS.GETERROR( ONUERRORCODE, OSBERRORMESSAGE );
      WHEN OTHERS THEN
         ERRORS.SETERROR;
         ERRORS.GETERROR( ONUERRORCODE, OSBERRORMESSAGE );
   END ACCEPTITEM;
   PROCEDURE REJECTITEM( ICLXMLREJECTITEM IN CLOB, ONUERRORCODE OUT GE_ERROR_LOG.ERROR_LOG_ID%TYPE, OSBERRORMESSAGE OUT GE_ERROR_LOG.DESCRIPTION%TYPE )
    IS
    BEGIN
      UT_TRACE.TRACE( 'Inicio - GE_BSAcceptItems.RejectItem', 1 );
      GE_BOUTILITIES.INITIALIZEOUTPUT( ONUERRORCODE, OSBERRORMESSAGE );
      GE_BOACEPTARITEMS.REJECTITEM( ICLXMLREJECTITEM );
      UT_TRACE.TRACE( 'Fin - GE_BSAcceptItems.RejectItem', 1 );
    EXCEPTION
      WHEN EX.CONTROLLED_ERROR THEN
         ERRORS.GETERROR( ONUERRORCODE, OSBERRORMESSAGE );
      WHEN OTHERS THEN
         ERRORS.SETERROR;
         ERRORS.GETERROR( ONUERRORCODE, OSBERRORMESSAGE );
   END REJECTITEM;
   FUNCTION FSBVERSION
    RETURN VARCHAR2
    IS
    BEGIN
      RETURN CSBVERSION;
   END;
END GE_BSACCEPTITEMS;
