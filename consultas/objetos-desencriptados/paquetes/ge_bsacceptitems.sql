   PROCEDURE ACCEPTITEM( ICLXMLACCEPTITEMS IN CLOB, ONUERRORCODE OUT GE_ERROR_LOG.ERROR_LOG_ID%TYPE, OSBERRORMESSAGE OUT GE_ERROR_LOG.DESCRIPTION%TYPE );
   PROCEDURE REJECTITEM( ICLXMLREJECTITEM IN CLOB, ONUERRORCODE OUT GE_ERROR_LOG.ERROR_LOG_ID%TYPE, OSBERRORMESSAGE OUT GE_ERROR_LOG.DESCRIPTION%TYPE );
