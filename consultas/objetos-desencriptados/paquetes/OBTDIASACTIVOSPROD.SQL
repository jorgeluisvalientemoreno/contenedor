
CREATE OR REPLACE FUNCTION OBTDIASACTIVOSPROD( INUMODORECLAMO IN NUMBER, INUCONSUMPERIOD IN PERICOSE.PECSCONS%TYPE )
 RETURN NUMBER
 IS
   NURETURNVALUE NUMBER;
 BEGIN
   PKERRORS.PUSH( 'ObtDiasActivosProd' );
   NURETURNVALUE := FA_BOSERVICIOSLIQPORPRODUCTO.FNUOBTDIASACTIVOSPROD( INUMODORECLAMO, INUCONSUMPERIOD );
   PKERRORS.POP;
   RETURN ( NURETURNVALUE );
 EXCEPTION
   WHEN OTHERS THEN
      PKERRORS.POP;
      RAISE;
END OBTDIASACTIVOSPROD;
/


