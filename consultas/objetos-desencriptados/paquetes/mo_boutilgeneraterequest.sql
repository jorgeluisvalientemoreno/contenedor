        EXCEPTION
            WHEN EX.CONTROLLED_ERROR THEN
                RAISE EX.CONTROLLED_ERROR;
            WHEN OTHERS THEN
                ERRORS.SETERROR;
                RAISE EX.CONTROLLED_ERROR;
        END FRCGETPRODMOTI;
    BEGIN
        UT_TRACE.TRACE('Inicio: [ MO_BOUtilGenerateRequest.GetRecMotiveBasic]',10);

        
        ORCMO_MOTIVE.MOTIVE_ID               := MO_BOSEQUENCES.FNUGETMOTIVEID;
