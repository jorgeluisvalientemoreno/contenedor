        ORCMO_MOTIVE.SUBCATEGORY_ID          := INUSUBCATEGORYID;
        ORCMO_MOTIVE.COMMERCIAL_PLAN_ID      := INUCOMMPLANID;
        ORCMO_MOTIVE.CAUSAL_ID               := INUMOTICAUSAL;
