SELECT 'MERGE INTO OPEN.LDC_PARAREPE A USING
 (SELECT
  '''||parecodi||''' as "PARECODI",
  '''||paradesc||''' as "PARADESC",
  '||NVL(TO_CHAR(parevanu),'NULL')||' as "PAREVANU",
  '''||paravast||''' as "PARAVAST"  
  FROM DUAL
) B
ON (A.PARECODI = B.PARECODI)
WHEN NOT MATCHED THEN 
INSERT (
  PARECODI, PAREVANU, PARAVAST, PARADESC)
VALUES (
  B.PARECODI, B.PAREVANU, B.PARAVAST, B.PARADESC)
WHEN MATCHED THEN
UPDATE SET 
  A.PAREVANU = B.PAREVANU,
  A.PARAVAST = B.PARAVAST,
  A.PARADESC = B.PARADESC;
/
COMMIT;
/'
from open.ldc_pararepe
where parecodi = 'LDC_PERSLERECA'
/