  WITH PRODUCTOS  AS (
        SELECT
            PRODUCT_ID - 3300000 PRODUCTO_GAS,
            PRODUCT_ID PRODUCTO_OSF,
            SUBSCRIPTION_ID CONTRATO_OSF,
            PRODUCT_STATUS_ID,
            SESUESTE
        FROM MIGRAGG.PR_PRODUCT P
        JOIN GASGG.SERVSUSC S ON SESUNUSE=P.PRODUCT_ID-3300000 AND SESUESTE NOT IN (11,14)
        WHERE product_status_id=2
         and not exists(select null from open.pr_prod_suspension s where s.product_id=P.PRODUCT_ID AND S.ACTIVE='Y')
    ), CAMBIOS_ESTADO AS (
        SELECT
            PRODUCTO_GAS,
            PRODUCTO_OSF,
            CONTRATO_OSF,
            SESUESTE,
            CA.CETSFECH REGISTER_DATE,
            CA.CETSFECH APLICATION_DATE,
            CA.CETSETAC ESTADO_TECNICO,
            ROW_NUMBER() OVER (PARTITION BY CA.CETSNUSE ORDER BY CA.CETSFECH DESC) AS RN
        FROM PRODUCTOS PR
        LEFT JOIN GASGG.CAESTESE CA ON CA.CETSNUSE = PR.PRODUCTO_GAS AND CA.CETSETAC = SESUESTE
    )
    SELECT *
    FROM CAMBIOS_ESTADO
    WHERE RN = 1;
