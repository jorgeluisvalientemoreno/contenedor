CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_DE_CC_FIN_REQ_CONCEPT AFTER INSERT OR UPDATE OR DELETE ON CC_FIN_REQ_CONCEPT
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

FINANCING_REQUEST_ID NUMBER(15);
TMP_BAL_BY_CONCEPT_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  FINANCING_REQUEST_ID := :new.FINANCING_REQUEST_ID;
  TMP_BAL_BY_CONCEPT_ID := :new.TMP_BAL_BY_CONCEPT_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  FINANCING_REQUEST_ID := :old.FINANCING_REQUEST_ID;
  TMP_BAL_BY_CONCEPT_ID := :old.TMP_BAL_BY_CONCEPT_ID;
  OPERATION := 'U';
ELSE
  FINANCING_REQUEST_ID := :old.FINANCING_REQUEST_ID;
  TMP_BAL_BY_CONCEPT_ID := :old.TMP_BAL_BY_CONCEPT_ID;
  OPERATION := 'D';
END IF;

INSERT INTO OPEN.LDCBI_CC_FIN_REQ_CONCEPT (
  FINANCING_REQUEST_ID,
  TMP_BAL_BY_CONCEPT_ID,
  OPERATION
)
VALUES (
  FINANCING_REQUEST_ID,
  TMP_BAL_BY_CONCEPT_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE_APPLICATION_ERROR(-20000,
                            'Error en TRG_DE_CC_FIN_REQ_CONCEPT por -->' || sqlcode ||
                            chr(13) || sqlerrm);
END;
