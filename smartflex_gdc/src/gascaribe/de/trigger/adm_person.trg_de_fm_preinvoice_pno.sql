CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_DE_FM_PREINVOICE_PNO
    AFTER INSERT OR UPDATE OR DELETE
    ON OPEN.FM_PREINVOICE_PNO
    REFERENCING OLD AS OLD NEW AS NEW
    FOR EACH ROW
BEGIN

    DECLARE

    PREINVOICE_PNO_ID NUMBER(15);
    OPERATION VARCHAR2(1);

    BEGIN

    IF INSERTING THEN
        PREINVOICE_PNO_ID := :new.PREINVOICE_PNO_ID;
        OPERATION := 'I';
    ELSIF UPDATING THEN
        PREINVOICE_PNO_ID := :old.PREINVOICE_PNO_ID;
        OPERATION := 'U';
    ELSE
        PREINVOICE_PNO_ID := :old.PREINVOICE_PNO_ID;
        OPERATION := 'D';
    END IF;

    INSERT INTO OPEN.LDCBI_FM_PREINVOICE_PNO (
        PREINVOICE_PNO_ID,
        OPERATION
    )
    VALUES (
        PREINVOICE_PNO_ID,
        OPERATION
    );

    EXCEPTION
    WHEN OTHERS THEN
        RAISE_APPLICATION_ERROR(-20000,
                                'Error en TRG_DE_FM_PREINVOICE_PNO por -->' || sqlcode ||
                                chr(13) || sqlerrm);
    END;

END;
