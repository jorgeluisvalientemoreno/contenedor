CREATE OR REPLACE TRIGGER OPEN.TRG_BI_OR_ORDER_OPEUNI_CHAN
    AFTER INSERT OR UPDATE OR DELETE
    ON OPEN.OR_ORDER_OPEUNI_CHAN
    REFERENCING OLD AS OLD NEW AS NEW
    FOR EACH ROW
BEGIN

    DECLARE

    ORDER_OPEUNI_CHAN_ID NUMBER(15);
    OPERATION VARCHAR2(1);

    BEGIN

    IF INSERTING THEN
        ORDER_OPEUNI_CHAN_ID := :new.ORDER_OPEUNI_CHAN_ID;
        OPERATION := 'I';
    ELSIF UPDATING THEN
        ORDER_OPEUNI_CHAN_ID := :old.ORDER_OPEUNI_CHAN_ID;
        OPERATION := 'U';
    ELSE
        ORDER_OPEUNI_CHAN_ID := :old.ORDER_OPEUNI_CHAN_ID;
        OPERATION := 'D';
    END IF;

    INSERT INTO OPEN.LDCBI_OR_ORDER_OPEUNI_CHAN (
        ORDER_OPEUNI_CHAN_ID,
        OPERATION
    )
    VALUES (
        ORDER_OPEUNI_CHAN_ID,
        OPERATION
    );

    EXCEPTION
    WHEN OTHERS THEN
        RAISE_APPLICATION_ERROR(-20000,
                                'Error en TRG_DE_OR_ORDER_OPEUNI_CHAN por -->' || sqlcode ||
                                chr(13) || sqlerrm);
    END;

END;
