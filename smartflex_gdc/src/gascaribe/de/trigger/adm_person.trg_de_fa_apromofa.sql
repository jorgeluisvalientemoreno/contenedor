CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_DE_FA_APROMOFA AFTER INSERT OR UPDATE OR DELETE ON FA_APROMOFA
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

APMOCONS NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  APMOCONS := :new.APMOCONS;
  OPERATION := 'I';
ELSIF UPDATING THEN
  APMOCONS := :old.APMOCONS;
  OPERATION := 'U';
ELSE
  APMOCONS := :old.APMOCONS;
  OPERATION := 'D';
END IF;

INSERT INTO OPEN.LDCBI_FA_APROMOFA (
  APMOCONS,
  OPERATION
)
VALUES (
  APMOCONS,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE_APPLICATION_ERROR(-20000,
                            'Error en TRG_DE_FA_APROMOFA por -->' || sqlcode ||
                            chr(13) || sqlerrm);
END;
