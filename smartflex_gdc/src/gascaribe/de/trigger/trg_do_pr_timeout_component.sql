CREATE OR REPLACE TRIGGER OPEN.TRG_DO_PR_TIMEOUT_COMPONENT
    AFTER INSERT OR UPDATE OR DELETE
    ON OPEN.PR_TIMEOUT_COMPONENT
    REFERENCING OLD AS OLD NEW AS NEW
    FOR EACH ROW
BEGIN

    DECLARE
    TIMEOUT_COMPONENT_ID NUMBER(15);
    OPERATION VARCHAR2(1);

    BEGIN

    IF INSERTING THEN
        TIMEOUT_COMPONENT_ID := :new.TIMEOUT_COMPONENT_ID;
        OPERATION := 'I';
    ELSIF UPDATING THEN
        TIMEOUT_COMPONENT_ID := :new.TIMEOUT_COMPONENT_ID;
        OPERATION := 'U';
    ELSE
        TIMEOUT_COMPONENT_ID := :old.TIMEOUT_COMPONENT_ID;
        OPERATION := 'D';
    END IF;

    INSERT INTO OPEN.LDCBI_PR_TIMEOUT_COMPONENT (
        TIMEOUT_COMPONENT_ID,
        OPERATION
    )
    VALUES (
        TIMEOUT_COMPONENT_ID,
        OPERATION
    );

    EXCEPTION
    WHEN OTHERS THEN
        RAISE_APPLICATION_ERROR(-20000,
                                'Error en TRG_DO_PR_TIMEOUT_COMPONENT por -->' || sqlcode ||
                                chr(13) || sqlerrm);
    END;

END;
