CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_DE_MO_MOT_PROMOTION AFTER INSERT OR UPDATE OR DELETE ON MO_MOT_PROMOTION
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

MOT_PROMOTION_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  MOT_PROMOTION_ID := :new.MOT_PROMOTION_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  MOT_PROMOTION_ID := :old.MOT_PROMOTION_ID;
  OPERATION := 'U';
ELSE
  MOT_PROMOTION_ID := :old.MOT_PROMOTION_ID;
  OPERATION := 'D';
END IF;

INSERT INTO OPEN.LDCBI_MO_MOT_PROMOTION (
  MOT_PROMOTION_ID,
  OPERATION
)
VALUES (
  MOT_PROMOTION_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE_APPLICATION_ERROR(-20000,
                            'Error en TRG_DE_MO_MOT_PROMOTION por -->' || sqlcode ||
                            chr(13) || sqlerrm);
END;
