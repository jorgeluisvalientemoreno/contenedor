CREATE TABLE OPEN.LDCBI_PR_PROMOTION (
    PROMOTION_ID   NUMBER(15)  NOT NULL,
    OPERATION      VARCHAR2(1) NOT NULL,
    PROCESS_STATUS NUMBER(3)   DEFAULT -1,
    CREATED_DATE   DATE        DEFAULT SYSDATE
);

-- Create/Recreate indexes
CREATE INDEX IDX_LDCBI_PR_PROMOTION ON OPEN.LDCBI_PR_PROMOTION (PROCESS_STATUS, CREATED_DATE) TABLESPACE TSI_DEFAULT;
CREATE INDEX IDX_LDCBI_PR_PROMOTION_PRST ON OPEN.LDCBI_PR_PROMOTION (PROCESS_STATUS) TABLESPACE TSI_DEFAULT;

-- Grant/Revoke object privileges
GRANT SELECT, INSERT, UPDATE, DELETE ON OPEN.LDCBI_PR_PROMOTION TO INNOVACIONBI;
GRANT SELECT, INSERT, UPDATE, DELETE ON OPEN.LDCBI_PR_PROMOTION TO INNOVACION;
GRANT SELECT, INSERT, UPDATE, DELETE ON OPEN.LDCBI_PR_PROMOTION TO DATAOPS;