CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_LD_ITEM_WORK_ORDER AFTER INSERT OR UPDATE OR DELETE ON LD_ITEM_WORK_ORDER
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

ITEM_WORK_ORDER_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  ITEM_WORK_ORDER_ID := :new.ITEM_WORK_ORDER_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  ITEM_WORK_ORDER_ID := :new.ITEM_WORK_ORDER_ID;
  OPERATION := 'U';
ELSE
  ITEM_WORK_ORDER_ID := :old.ITEM_WORK_ORDER_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_LD_ITEM_WORK_ORDER (
  ITEM_WORK_ORDER_ID,
  OPERATION
)
VALUES (
  ITEM_WORK_ORDER_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
