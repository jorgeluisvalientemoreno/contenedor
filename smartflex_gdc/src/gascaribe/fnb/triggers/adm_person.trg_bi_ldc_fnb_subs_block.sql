CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_LDC_FNB_SUBS_BLOCK AFTER INSERT OR UPDATE OR DELETE ON LDC_FNB_SUBS_BLOCK
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

USER_BLOCK_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  USER_BLOCK_ID := :new.USER_BLOCK_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  USER_BLOCK_ID := :new.USER_BLOCK_ID;
  OPERATION := 'U';
ELSE
  USER_BLOCK_ID := :old.USER_BLOCK_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_LDC_FNB_SUBS_BLOCK (
  USER_BLOCK_ID,
  OPERATION
)
VALUES (
  USER_BLOCK_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
