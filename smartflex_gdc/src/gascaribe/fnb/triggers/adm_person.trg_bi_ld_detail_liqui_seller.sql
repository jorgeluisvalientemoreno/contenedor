CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_LD_DETAIL_LIQUI_SELLER AFTER INSERT OR UPDATE OR DELETE ON LD_DETAIL_LIQUI_SELLER
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

DETAIL_LIQUI_SELLER_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  DETAIL_LIQUI_SELLER_ID := :new.DETAIL_LIQUI_SELLER_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  DETAIL_LIQUI_SELLER_ID := :new.DETAIL_LIQUI_SELLER_ID;
  OPERATION := 'U';
ELSE
  DETAIL_LIQUI_SELLER_ID := :old.DETAIL_LIQUI_SELLER_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_LD_DETAIL_LIQUI_SELLER (
  DETAIL_LIQUI_SELLER_ID,
  OPERATION
)
VALUES (
  DETAIL_LIQUI_SELLER_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
