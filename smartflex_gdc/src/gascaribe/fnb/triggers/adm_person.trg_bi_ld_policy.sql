CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_LD_POLICY AFTER INSERT OR UPDATE OR DELETE ON LD_POLICY
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

POLICY_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  POLICY_ID := :new.POLICY_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  POLICY_ID := :new.POLICY_ID;
  OPERATION := 'U';
ELSE
  POLICY_ID := :old.POLICY_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_LD_POLICY (
  POLICY_ID,
  OPERATION
)
VALUES (
  POLICY_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
