DECLARE
  nuConta NUMBER;
BEGIN
  SELECT COUNT(*) INTO nuConta
  FROM dba_objects
  WHERE object_name = 'LDC_FNCONSULTAOTESDOC'
   AND OWNER = 'ADM_PERSON'
   AND OBJECT_TYPE <> 'SYNONYM';
   
  IF nuConta > 0 then
    EXECUTE IMMEDIATE 'DROP FUNCTION ADM_PERSON.LDC_FNCONSULTAOTESDOC';
  END IF;  
END;
/