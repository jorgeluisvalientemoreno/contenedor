DECLARE
	nuConta NUMBER;
BEGIN
	SELECT 	COUNT(1) 
	INTO 	nuConta
	FROM 	dba_tables
	WHERE 	table_name = 'LDC_TIPOCAUSALCARDIF';
  
	IF (nuConta = 0) THEN  
		EXECUTE IMMEDIATE 'CREATE TABLE LDC_TIPOCAUSALCARDIF(  
														CAUSAL_CARDIF 	NUMBER(4) PRIMARY KEY,
														TIPO_CAUSAL   	NUMBER(4) NOT NULL)';
														
		EXECUTE IMMEDIATE 'COMMENT ON COLUMN LDC_TIPOCAUSALCARDIF.CAUSAL_CARDIF IS ''CAUSAL CARDIF''';
		EXECUTE IMMEDIATE 'COMMENT ON COLUMN LDC_TIPOCAUSALCARDIF.TIPO_CAUSAL IS ''TIPO DE CAUSAL''';
		EXECUTE IMMEDIATE 'COMMENT ON TABLE LDC_TIPOCAUSALCARDIF IS ''TIPO DE CAUSAL DE LA CAUSAL CARDIF''';
	
	END IF;
  
	EXECUTE IMMEDIATE 'grant select, insert, delete, update on LDC_TIPOCAUSALCARDIF to SYSTEM_OBJ_PRIVS_ROLE';
	EXECUTE IMMEDIATE 'grant select on LDC_TIPOCAUSALCARDIF to RSELOPEN';
	EXECUTE IMMEDIATE 'grant select on LDC_TIPOCAUSALCARDIF to reportes';
END;
