DECLARE
  nuConta NUMBER;
BEGIN

  SELECT COUNT(*) INTO nuConta
  FROM dba_objects
  WHERE object_name = 'CAMBIOCATEGORIAPROD'
   AND OWNER = 'ADM_PERSON'
   AND OBJECT_TYPE = 'SYNONYM';
   
  IF nuConta > 0 then
    EXECUTE IMMEDIATE 'DROP SYNONYM ADM_PERSON.CAMBIOCATEGORIAPROD';
  END IF;  



  SELECT COUNT(*) INTO nuConta
  FROM dba_objects
  WHERE object_name = 'CAMBIOCATEGORIAPROD'
   AND OWNER = 'OPEN'
   AND OBJECT_TYPE = 'PROCEDURE';
   
  IF nuConta > 0 then
    EXECUTE IMMEDIATE 'DROP PROCEDURE OPEN.CAMBIOCATEGORIAPROD';
  END IF;  
END;
/