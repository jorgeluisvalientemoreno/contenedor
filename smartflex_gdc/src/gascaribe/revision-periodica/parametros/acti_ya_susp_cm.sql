SET DEFINE OFF;
MERGE INTO OPEN.LD_PARAMETER A USING
 (SELECT
  'ACTI_YA_SUSP_CM' as PARAMETER_ID,
  100006650 as NUMERIC_VALUE,
  NULL as VALUE_CHAIN,
  'ACTIVIDAD A GENERAR CUANDO EL PRODUCTO YA SE ENCUENTRA SUSPENDIDO EN CENTRO DE MEDICION' as DESCRIPTION
  FROM DUAL) B
ON (A.PARAMETER_ID = B.PARAMETER_ID)
WHEN NOT MATCHED THEN 
INSERT (
  PARAMETER_ID, NUMERIC_VALUE, VALUE_CHAIN, DESCRIPTION)
VALUES (
  B.PARAMETER_ID, B.NUMERIC_VALUE, B.VALUE_CHAIN, B.DESCRIPTION)
WHEN MATCHED THEN
UPDATE SET 
  A.NUMERIC_VALUE = B.NUMERIC_VALUE,
  A.VALUE_CHAIN = B.VALUE_CHAIN,
  A.DESCRIPTION = B.DESCRIPTION;

COMMIT;
