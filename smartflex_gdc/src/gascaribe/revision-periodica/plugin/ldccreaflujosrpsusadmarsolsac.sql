PROMPT BORRAR PROCEDIMIENTO LDCCREAFLUJOSRPSUSADMARSOLSAC
DECLARE
  nuConta NUMBER;
BEGIN
  SELECT COUNT(*) INTO nuConta
  FROM dba_objects
  WHERE object_name = 'LDCCREAFLUJOSRPSUSADMARSOLSAC'
   AND OWNER = 'OPEN'
   AND OBJECT_TYPE <> 'SYNONYM';
   
  IF nuConta > 0 then
    EXECUTE IMMEDIATE 'DROP PROCEDURE LDCCREAFLUJOSRPSUSADMARSOLSAC';
    dbms_output.put_line('PROCEDURE LDCCREAFLUJOSRPSUSADMARSOLSAC BORRADO');
  END IF;
EXCEPTION
    WHEN OTHERS THEN 
        dbms_output.put_line('No se pudo borrar el procedimiento LDCCREAFLUJOSRPSUSADMARSOLSAC, '||sqlerrm); 
END;
