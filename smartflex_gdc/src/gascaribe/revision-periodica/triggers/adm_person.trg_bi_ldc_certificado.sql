CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_LDC_CERTIFICADO AFTER INSERT OR UPDATE OR DELETE ON LDC_CERTIFICADO
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

ID_CERTIFICADO NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  ID_CERTIFICADO := :new.ID_CERTIFICADO;
  OPERATION := 'I';
ELSIF UPDATING THEN
  ID_CERTIFICADO := :new.ID_CERTIFICADO;
  OPERATION := 'U';
ELSE
  ID_CERTIFICADO := :old.ID_CERTIFICADO;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_LDC_CERTIFICADO (
  ID_CERTIFICADO,
  OPERATION
)
VALUES (
  ID_CERTIFICADO,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
