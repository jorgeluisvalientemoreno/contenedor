set serveroutput on;
PROMPT BORRAR PROCEDIMIENTO LDC_PRITEM_TASKTYPE_FILE 
DECLARE
  nuConta NUMBER;
BEGIN
  SELECT COUNT(*) INTO nuConta
  FROM dba_objects
  WHERE object_name = 'LDC_PRITEM_TASKTYPE_FILE'
   AND OWNER = 'OPEN'
   AND OBJECT_TYPE = 'PROCEDURE';
   
  IF nuConta > 0 then
    EXECUTE IMMEDIATE 'DROP PROCEDURE OPEN.LDC_PRITEM_TASKTYPE_FILE';
  END IF;  
   
END;
/