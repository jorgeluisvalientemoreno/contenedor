DECLARE
  nuConta NUMBER;
BEGIN
  SELECT COUNT(1) INTO nuConta
  FROM DBA_SYNONYMS
  WHERE SYNONYM_NAME = 'OS_REGISTERREQUESTWITHXML'
   AND OWNER = 'ADM_PERSON';

  IF nuConta = 0 THEN
      EXECUTE IMMEDIATE 'CREATE SYNONYM ADM_PERSON.OS_REGISTERREQUESTWITHXML FOR OPEN.OS_REGISTERREQUESTWITHXML';
  END IF;
END;
/