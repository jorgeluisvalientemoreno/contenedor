DECLARE
  nuConta NUMBER;
BEGIN
  SELECT COUNT(1) INTO nuConta
  FROM DBA_SYNONYMS
  WHERE SYNONYM_NAME = 'PKBOPROCCTRLBYBILLPERIOD'
   AND OWNER = 'ADM_PERSON';

  IF nuConta = 0 THEN
      EXECUTE IMMEDIATE 'CREATE SYNONYM ADM_PERSON.PKBOPROCCTRLBYBILLPERIOD FOR OPEN.PKBOPROCCTRLBYBILLPERIOD';
  END IF;
END;
/