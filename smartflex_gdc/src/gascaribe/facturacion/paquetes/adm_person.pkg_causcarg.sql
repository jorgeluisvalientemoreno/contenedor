    -- Borra un registro
    PROCEDURE prBorRegistro(
        inuCACACODI    NUMBER
    );
 
    -- Borra un registro por RowId
    PROCEDURE prBorRegistroxRowId(
        iRowId ROWID
    );
 
    -- Obtiene el valor de la columna CACADESC
    FUNCTION fsbObtCACADESC(
        inuCACACODI    NUMBER
        ) RETURN causcarg.CACADESC%TYPE;
 
    -- Actualiza el valor de la columna CACADESC
