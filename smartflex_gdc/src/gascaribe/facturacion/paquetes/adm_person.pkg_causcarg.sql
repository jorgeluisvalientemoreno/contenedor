    CURSOR cuRegistroRId
    (
        inuCACACODI    NUMBER
    )
    IS
        SELECT tb.*, tb.Rowid
        FROM causcarg tb
        WHERE
        CACACODI = inuCACACODI;
     
    CURSOR cuRegistroRIdLock
    (
        inuCACACODI    NUMBER
    )
    IS
        SELECT tb.*, tb.Rowid
        FROM causcarg tb
        WHERE
        CACACODI = inuCACACODI
        FOR UPDATE NOWAIT;
     
    -- Obtiene el registro y el rowid
    FUNCTION frcObtRegistroRId(
        inuCACACODI    NUMBER, iblBloquea BOOLEAN DEFAULT FALSE
    ) RETURN cuRegistroRId%ROWTYPE;
 
