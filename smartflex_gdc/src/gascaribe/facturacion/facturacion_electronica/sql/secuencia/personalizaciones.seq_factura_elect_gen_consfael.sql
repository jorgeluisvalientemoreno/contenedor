DECLARE
  nuConta NUMBER;
BEGIN

 SELECT COUNT(*) INTO nuConta
 FROM DBA_SEQUENCES
 WHERE SEQUENCE_NAME = 'SEQ_FACTURA_ELECT_GEN_CONSFAEL' 
    AND sequence_owner = 'PERSONALIZACIONES';
  
  IF nuConta = 0 THEN

	EXECUTE IMMEDIATE 'CREATE SEQUENCE PERSONALIZACIONES.SEQ_FACTURA_ELECT_GEN_CONSFAEL INCREMENT BY 1	START WITH 1 NOCYCLE NOCACHE';
  EXECUTE IMMEDIATE 'GRANT SELECT ON  PERSONALIZACIONES.SEQ_FACTURA_ELECT_GEN_CONSFAEL TO SYSTEM_OBJ_PRIVS_ROLE';

  END IF;
END;
/