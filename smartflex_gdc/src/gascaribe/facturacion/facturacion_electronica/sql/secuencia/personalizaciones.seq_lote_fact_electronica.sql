DECLARE
  nuConta NUMBER;
BEGIN

 SELECT COUNT(*) INTO nuConta
 FROM DBA_SEQUENCES
 WHERE SEQUENCE_NAME = 'SEQ_LOTE_FACT_ELECTRONICA' 
    AND sequence_owner = 'PERSONALIZACIONES';
  
  IF nuConta = 0 THEN

	EXECUTE IMMEDIATE 'CREATE SEQUENCE PERSONALIZACIONES.SEQ_LOTE_FACT_ELECTRONICA INCREMENT BY 1	START WITH 1 NOCYCLE NOCACHE';
  EXECUTE IMMEDIATE 'GRANT SELECT ON  PERSONALIZACIONES.SEQ_LOTE_FACT_ELECTRONICA TO SYSTEM_OBJ_PRIVS_ROLE';

  END IF;
END;
