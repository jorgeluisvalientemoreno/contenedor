DECLARE
  nuConta NUMBER;
BEGIN

 SELECT COUNT(*) INTO nuConta
 FROM DBA_SEQUENCES
 WHERE SEQUENCE_NAME = 'SEQ_TIDOFAEL' 
    AND sequence_owner = 'OPEN';
  
  IF nuConta = 0 THEN

	EXECUTE IMMEDIATE 'CREATE SEQUENCE SEQ_TIDOFAEL INCREMENT BY 1	START WITH 1 NOCYCLE NOCACHE';
  EXECUTE IMMEDIATE 'GRANT SELECT ON  SEQ_TIDOFAEL TO SYSTEM_OBJ_PRIVS_ROLE';

  END IF;
END;
/