MERGE INTO PARAMETROS A USING
 (SELECT
  'ACTIVAR_ENVIO_MAIL_FAEL' as CODIGO,
  'Activar envió de email en la facturación electrónica' as DESCRIPCION,
  null as VALOR_NUMERICO,
  'S'  as VALOR_CADENA,
  NULL as VALOR_FECHA,
  18 as PROCESO,
  SYSDATE as FECHA_CREACION,
  NULL as FECHA_ACTUALIZACION,
  USER as USUARIO,
  PKG_SESSION.FSBGETTERMINAL as TERMINAL,
  'A' ESTADO, 
  'S' OBLIGATORIO
  FROM DUAL) B
ON (A.CODIGO = B.CODIGO)
WHEN NOT MATCHED THEN 
INSERT (
  CODIGO, DESCRIPCION, VALOR_NUMERICO, VALOR_CADENA, VALOR_FECHA, 
  PROCESO, FECHA_CREACION, FECHA_ACTUALIZACION, USUARIO, TERMINAL, ESTADO, OBLIGATORIO)
VALUES (
  B.CODIGO, B.DESCRIPCION, B.VALOR_NUMERICO, B.VALOR_CADENA, B.VALOR_FECHA, 
  B.PROCESO, B.FECHA_CREACION, B.FECHA_ACTUALIZACION, B.USUARIO, B.TERMINAL, B.ESTADO, B.OBLIGATORIO)
WHEN MATCHED THEN
UPDATE SET 
  A.DESCRIPCION = B.DESCRIPCION,
  A.VALOR_NUMERICO = B.VALOR_NUMERICO,
  A.VALOR_CADENA = B.VALOR_CADENA,
  A.VALOR_FECHA = B.VALOR_FECHA,
  A.PROCESO = B.PROCESO,
  A.FECHA_CREACION = B.FECHA_CREACION,
  A.FECHA_ACTUALIZACION = B.FECHA_ACTUALIZACION,
  A.USUARIO = B.USUARIO,
  A.TERMINAL = B.TERMINAL;
COMMIT
