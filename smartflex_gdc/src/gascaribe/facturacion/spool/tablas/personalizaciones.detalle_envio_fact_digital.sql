DECLARE
  CURSOR cuTabla(sbTable VARCHAR2) IS
  SELECT COUNT(1) FROM all_tables WHERE upper(TABLE_NAME) = upper(sbTable);

  nuExistsTable NUMBER; 

BEGIN

  --Validacion de existencia de entidad
  OPEN cuTabla('detalle_envio_fact_digital');
  FETCH cuTabla INTO nuExistsTable;

    IF (nuExistsTable <> 0) THEN
      /*Si la tabla ya existe, se informa en consola acerca de su existencia*/
      dbms_output.put_Line('La Tabla DETALLE_ENVIO_FACT_DIGITAL ya existe, se procede a borrar');
      EXECUTE IMMEDIATE 'drop table PERSONALIZACIONES.DETALLE_ENVIO_FACT_DIGITAL';
    END IF;    

  
    EXECUTE IMMEDIATE 'CREATE TABLE "PERSONALIZACIONES"."DETALLE_ENVIO_FACT_DIGITAL"
                      (
                          "CONTRATO" NUMBER(8) NOT NULL ENABLE, 
	                        "FACTURA" NUMBER(10) NOT NULL ENABLE, 
                          "PERIODO_FACTURACION" NUMBER(6), 
                          "FECHA_REGISTRO" DATE NOT NULL ENABLE 
                      )';

    EXECUTE IMMEDIATE 'CREATE INDEX PERSONALIZACIONES.IDX_DT_ENVIO_FACT_DIGITAL_01 on PERSONALIZACIONES.DETALLE_ENVIO_FACT_DIGITAL(CONTRATO,FACTURA,PERIODO_FACTURACION)';
    EXECUTE IMMEDIATE 'CREATE INDEX PERSONALIZACIONES.IDX_DT_ENVIO_FACT_DIGITAL_02 on PERSONALIZACIONES.DETALLE_ENVIO_FACT_DIGITAL(CONTRATO)';
    
    --- Comentarios de la tabla y campos.
    EXECUTE IMMEDIATE 'COMMENT ON COLUMN "PERSONALIZACIONES"."DETALLE_ENVIO_FACT_DIGITAL"."CONTRATO" IS ''Contrato''';
    EXECUTE IMMEDIATE 'COMMENT ON COLUMN "PERSONALIZACIONES"."DETALLE_ENVIO_FACT_DIGITAL"."FACTURA" IS ''Factura''';
    EXECUTE IMMEDIATE 'COMMENT ON COLUMN "PERSONALIZACIONES"."DETALLE_ENVIO_FACT_DIGITAL"."PERIODO_FACTURACION" IS ''Periodo de Facturación''';
    EXECUTE IMMEDIATE 'COMMENT ON COLUMN "PERSONALIZACIONES"."DETALLE_ENVIO_FACT_DIGITAL"."FECHA_REGISTRO" IS ''Fecha de Registro''';
	  EXECUTE IMMEDIATE 'COMMENT ON TABLE "PERSONALIZACIONES"."DETALLE_ENVIO_FACT_DIGITAL"  IS ''Detalle Facturas que aplican para envió digital''';
  
    --- Aplica Permisos.
    BEGIN
      pkg_utilidades.prAplicarPermisos('DETALLE_ENVIO_FACT_DIGITAL', 'PERSONALIZACIONES');
    END;
    --- Fin.
  CLOSE cuTabla;

END;
