CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_PERICOSE AFTER INSERT OR UPDATE OR DELETE ON PERICOSE
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

PECSCONS NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  PECSCONS := :new.PECSCONS;
  OPERATION := 'I';
ELSIF UPDATING THEN
  PECSCONS := :new.PECSCONS;
  OPERATION := 'U';
ELSE
  PECSCONS := :old.PECSCONS;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_PERICOSE (
  PECSCONS,
  OPERATION
)
VALUES (
  PECSCONS,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
