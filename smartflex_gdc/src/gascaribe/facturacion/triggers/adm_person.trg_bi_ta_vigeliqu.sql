CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_TA_VIGELIQU AFTER INSERT OR UPDATE OR DELETE ON TA_VIGELIQU
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

VILICONS NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  VILICONS := :new.VILICONS;
  OPERATION := 'I';
ELSIF UPDATING THEN
  VILICONS := :new.VILICONS;
  OPERATION := 'U';
ELSE
  VILICONS := :old.VILICONS;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_TA_VIGELIQU (
  VILICONS,
  OPERATION
)
VALUES (
  VILICONS,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
