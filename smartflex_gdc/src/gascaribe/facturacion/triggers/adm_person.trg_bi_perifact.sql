CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_PERIFACT AFTER INSERT OR UPDATE OR DELETE ON PERIFACT
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

PEFACODI NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  PEFACODI := :new.PEFACODI;
  OPERATION := 'I';
ELSIF UPDATING THEN
  PEFACODI := :new.PEFACODI;
  OPERATION := 'U';
ELSE
  PEFACODI := :old.PEFACODI;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_PERIFACT (
  PEFACODI,
  OPERATION
)
VALUES (
  PEFACODI,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
