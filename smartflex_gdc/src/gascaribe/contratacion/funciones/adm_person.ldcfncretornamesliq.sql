DECLARE
  nuConta NUMBER;
BEGIN
  SELECT COUNT(*) INTO nuConta
  FROM dba_objects
  WHERE object_name = 'LDCFNCRETORNAMESLIQ'
   AND OWNER = 'ADM_PERSON'
   AND OBJECT_TYPE = 'FUNCTION';
   
  IF nuConta > 0 then
    EXECUTE IMMEDIATE 'DROP FUNCTION ADM_PERSON.LDCFNCRETORNAMESLIQ';
  END IF;  
END;
/