BEGIN

	INSERT INTO COPRSUCA(CPSCCATE, CPSCSUCA, CPSCTCON, CPSCUBGE, CPSCANCO, CPSCMECO, CPSCCONS, CPSCPROD, CPSCPRDI, CPSCCOTO)
	SELECT CPSCCATE, CPSCSUCA, 1, CPSCUBGE, CPSCANCO, CPSCMECO, SQ_COPRSUCA_198726.NEXTVAL, CPSCPROD, CPSCPRDI, CPSCCOTO
	FROM OPEN.LDC_COPRSUCA L
	WHERE NOT EXISTS (SELECT 'X' 
					  FROM OPEN.COPRSUCA A 
					  WHERE A.cpsccate = L.cpsccate 
					  AND A.cpscsuca = L.cpscsuca 
					  AND A.cpscubge = L.cpscubge
					  AND A.cpscanco = L.cpscanco 
					  AND A.cpscmeco = L.cpscmeco);
  
    COMMIT;

END;
