MERGE INTO LD_PARAMETER A USING
(SELECT
    'TIPOS_IDENT_NUMERICAS' AS "PARAMETER_ID", 
    NULL AS "NUMERIC_VALUE",
    '1,3,4,110' AS "VALUE_CHAIN", 
    'Almacena los tipos de identificación que solo pueden ser numéricas para la evaluación del trigger TRG_VALIDA_IDENT_CLIENTE' AS "DESCRIPTION"
FROM DUAL) B
ON (A.PARAMETER_ID = B.PARAMETER_ID)
WHEN NOT MATCHED THEN 
INSERT (
  PARAMETER_ID, NUMERIC_VALUE, VALUE_CHAIN, DESCRIPTION)
VALUES (
  B.PARAMETER_ID, B.NUMERIC_VALUE, B.VALUE_CHAIN, B.DESCRIPTION)
WHEN MATCHED THEN
UPDATE SET   
  A.NUMERIC_VALUE = B.NUMERIC_VALUE,
  A.VALUE_CHAIN = B.VALUE_CHAIN,
  A.DESCRIPTION = B.DESCRIPTION;

COMMIT;
