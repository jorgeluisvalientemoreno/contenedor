DECLARE
  nuConta NUMBER;
BEGIN
  SELECT COUNT(*) INTO nuConta
  FROM dba_objects
  WHERE object_name = 'LDC_INSACTCALLCENTER'
   AND OWNER = 'ADM_PERSON'
   AND OBJECT_TYPE = 'PROCEDURE';
   
  IF nuConta > 0 then
    EXECUTE IMMEDIATE 'DROP PROCEDURE ADM_PERSON.LDC_INSACTCALLCENTER';
  END IF;  
END;
/