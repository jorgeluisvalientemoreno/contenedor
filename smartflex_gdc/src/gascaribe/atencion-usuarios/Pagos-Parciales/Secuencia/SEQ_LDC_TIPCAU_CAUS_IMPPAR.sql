DECLARE
 nuTab1 NUMBER := 0;
BEGIN
 SELECT COUNT(1) INTO nuTab1
   FROM all_sequences l 
  WHERE l.SEQUENCE_NAME = 'SEQ_LDC_TIPCAU_CAUS_IMPPAR';
  IF (nuTab1 = 0) THEN
   EXECUTE IMMEDIATE 'CREATE SEQUENCE SEQ_LDC_TIPCAU_CAUS_IMPPAR INCREMENT BY 1 START WITH 1 NOCACHE';
   EXECUTE IMMEDIATE 'GRANT ALL ON SEQ_LDC_TIPCAU_CAUS_IMPPAR TO SYSTEM_OBJ_PRIVS_ROLE';
   EXECUTE IMMEDIATE 'GRANT SELECT ON SEQ_LDC_TIPCAU_CAUS_IMPPAR TO REPORTES';
   EXECUTE IMMEDIATE 'GRANT SELECT ON SEQ_LDC_TIPCAU_CAUS_IMPPAR TO RSELOPEN';   
  END IF;
END;
