Prompt Ejecutando borrado de Job Scheduler BAJAR_RECLAMOS_PQR_ESCRITAS
DECLARE

    CURSOR cuDBA_SCHEDULER_JOBS
    IS
    SELECT job_name
    FROM DBA_SCHEDULER_JOBS
    WHERE OWNER = 'OPEN'
    AND JOB_NAME = 'BAJAR_RECLAMOS_PQR_ESCRITAS';
    
    rcDBA_SCHEDULER_JOBS    cuDBA_SCHEDULER_JOBS%ROWTYPE;
    
BEGIN

    OPEN cuDBA_SCHEDULER_JOBS;
    FETCH cuDBA_SCHEDULER_JOBS INTO rcDBA_SCHEDULER_JOBS;
    CLOSE cuDBA_SCHEDULER_JOBS;
    
    IF rcDBA_SCHEDULER_JOBS.JOB_NAME IS NOT NULL THEN   
        DBMS_SCHEDULER.DROP_JOB ( job_name => 'OPEN.BAJAR_RECLAMOS_PQR_ESCRITAS', force => TRUE);
        DBMS_OUTPUT.PUT_LINE( 'Borrado Job Scheduler OPEN.BAJAR_RECLAMOS_PQR_ESCRITAS' );
    ELSE
        DBMS_OUTPUT.PUT_LINE( 'No existe el Job Scheduler OPEN.BAJAR_RECLAMOS_PQR_ESCRITAS' );
    END IF;
    
    EXCEPTION
        WHEN OTHERS THEN
            DBMS_OUTPUT.PUT_LINE( 'Error|' || SQLERRM );               
END;
/

