MERGE INTO PERSONALIZACIONES.PROCESO_NEGOCIO A USING
 (SELECT
  1 as "CODIGO",
  'SERVICIOS NUEVOS' as "DESCRIPCION"
  FROM DUAL) B
ON (A.CODIGO = B.CODIGO)
WHEN NOT MATCHED THEN 
INSERT (
  CODIGO, DESCRIPCION)
VALUES (
  B.CODIGO, B.DESCRIPCION)
WHEN MATCHED THEN
UPDATE SET 
  A.DESCRIPCION = B.DESCRIPCION;

COMMIT;
