MERGE INTO OPEN.LD_PARAMETER A USING
 (SELECT
  'COD_PERI_GRACIA_PILOTO_SN_CES' as "PARAMETER_ID",
  47 as "NUMERIC_VALUE",
  null as "VALUE_CHAIN",
  'CODIGO DEL PERIODO DE GRACIA PARA EL PILOTO DE SERVICIOS NUEVOS CESAR SN:611' as "DESCRIPTION"
  FROM DUAL) B
ON (A.PARAMETER_ID = B.PARAMETER_ID)
WHEN NOT MATCHED THEN 
INSERT (
  PARAMETER_ID, NUMERIC_VALUE, VALUE_CHAIN, DESCRIPTION)
VALUES (
  B.PARAMETER_ID, B.NUMERIC_VALUE, B.VALUE_CHAIN, B.DESCRIPTION)
WHEN MATCHED THEN
UPDATE SET 
  A.NUMERIC_VALUE = B.NUMERIC_VALUE,
  A.VALUE_CHAIN = B.VALUE_CHAIN,
  A.DESCRIPTION = B.DESCRIPTION;
/
COMMIT;
