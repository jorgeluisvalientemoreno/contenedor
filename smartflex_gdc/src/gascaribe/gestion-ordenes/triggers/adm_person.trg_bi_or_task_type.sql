CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_OR_TASK_TYPE AFTER INSERT OR UPDATE OR DELETE ON OR_TASK_TYPE
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

TASK_TYPE_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  TASK_TYPE_ID := :new.TASK_TYPE_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  TASK_TYPE_ID := :new.TASK_TYPE_ID;
  OPERATION := 'U';
ELSE
  TASK_TYPE_ID := :old.TASK_TYPE_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_OR_TASK_TYPE (
  TASK_TYPE_ID,
  OPERATION
)
VALUES (
  TASK_TYPE_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
