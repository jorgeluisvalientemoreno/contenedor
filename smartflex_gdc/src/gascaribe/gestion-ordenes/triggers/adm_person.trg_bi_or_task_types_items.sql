CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_OR_TASK_TYPES_ITEMS AFTER INSERT OR UPDATE OR DELETE ON OR_TASK_TYPES_ITEMS
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

ITEMS_ID NUMBER(15);
TASK_TYPE_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  ITEMS_ID := :new.ITEMS_ID;
  TASK_TYPE_ID := :new.TASK_TYPE_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  ITEMS_ID := :new.ITEMS_ID;
  TASK_TYPE_ID := :new.TASK_TYPE_ID;
  OPERATION := 'U';
ELSE
  ITEMS_ID := :old.ITEMS_ID;
  TASK_TYPE_ID := :old.TASK_TYPE_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_OR_TASK_TYPES_ITEMS (
  ITEMS_ID,
  TASK_TYPE_ID,
  OPERATION
)
VALUES (
  ITEMS_ID,
  TASK_TYPE_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
