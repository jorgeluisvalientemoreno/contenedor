CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_OR_TASK_TYPE_CAUSAL AFTER INSERT OR UPDATE OR DELETE ON OR_TASK_TYPE_CAUSAL
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

CAUSAL_ID NUMBER(15);
TASK_TYPE_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  CAUSAL_ID := :new.CAUSAL_ID;
  TASK_TYPE_ID := :new.TASK_TYPE_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  CAUSAL_ID := :new.CAUSAL_ID;
  TASK_TYPE_ID := :new.TASK_TYPE_ID;
  OPERATION := 'U';
ELSE
  CAUSAL_ID := :old.CAUSAL_ID;
  TASK_TYPE_ID := :old.TASK_TYPE_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_OR_TASK_TYPE_CAUSAL (
  CAUSAL_ID,
  TASK_TYPE_ID,
  OPERATION
)
VALUES (
  CAUSAL_ID,
  TASK_TYPE_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
