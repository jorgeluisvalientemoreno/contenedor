DECLARE
  nuConta NUMBER;
BEGIN
  SELECT COUNT(1) INTO nuConta
  FROM DBA_SYNONYMS
  WHERE SYNONYM_NAME = 'API_ASSIGN_ORDER'
   AND OWNER = 'PERSONALIZACIONES';

  IF nuConta = 0 THEN
      EXECUTE IMMEDIATE 'CREATE SYNONYM PERSONALIZACIONES.API_ASSIGN_ORDER FOR ADM_PERSON.API_ASSIGN_ORDER';
  END IF;
END;
/

