DECLARE
  CURSOR cuTabla(sbTable VARCHAR2) IS
    SELECT * FROM all_tables WHERE upper(TABLE_NAME) = upper(sbTable);

  rccuTabla cuTabla%ROWTYPE;

BEGIN

  --Validacion de existencia de entidad
  OPEN cuTabla('homologacion_servicios');
  FETCH cuTabla
    INTO rccuTabla;

  IF (cuTabla%NOTFOUND) THEN
  
    EXECUTE IMMEDIATE 'CREATE TABLE "PERSONALIZACIONES"."HOMOLOGACION_SERVICIOS"
   ("ESQUEMA_ORIGEN" VARCHAR2(500 BYTE) NOT NULL ENABLE, 
	"SERVICIO_ORIGEN" VARCHAR2(500 BYTE) NOT NULL ENABLE, 
	"DESCRIPCION_ORIGEN" VARCHAR2(4000 BYTE), 
	"ESQUEMA_DESTINO" VARCHAR2(500 BYTE) NOT NULL ENABLE, 
	"SERVICIO_DESTINO" VARCHAR2(500 BYTE) NOT NULL ENABLE, 
	"DESCRIPCION_DESTINO" VARCHAR2(4000 BYTE), 
	"OBSERVACION" VARCHAR2(4000 BYTE) 
   )';
    --- Comentarios de la tabla y campos.
    EXECUTE IMMEDIATE 'COMMENT ON COLUMN "PERSONALIZACIONES"."HOMOLOGACION_SERVICIOS"."ESQUEMA_ORIGEN" IS ''NOMBRE ESQUEMA ORIGEN''';
    EXECUTE IMMEDIATE 'COMMENT ON COLUMN "PERSONALIZACIONES"."HOMOLOGACION_SERVICIOS"."SERVICIO_ORIGEN" IS ''NOMBRE SERVICIO ORIGEN''';
    EXECUTE IMMEDIATE 'COMMENT ON COLUMN "PERSONALIZACIONES"."HOMOLOGACION_SERVICIOS"."DESCRIPCION_ORIGEN" IS ''DESCRIPCION DEL SERVICIO ORIGEN''';
    EXECUTE IMMEDIATE 'COMMENT ON COLUMN "PERSONALIZACIONES"."HOMOLOGACION_SERVICIOS"."ESQUEMA_DESTINO" IS ''NOMBRE ESQUEMA DESTINO''';
    EXECUTE IMMEDIATE 'COMMENT ON COLUMN "PERSONALIZACIONES"."HOMOLOGACION_SERVICIOS"."SERVICIO_DESTINO" IS ''NOMBRE SERVICIO DESTINO''';    
    EXECUTE IMMEDIATE 'COMMENT ON COLUMN "PERSONALIZACIONES"."HOMOLOGACION_SERVICIOS"."DESCRIPCION_DESTINO" IS ''DESCRIPCION DEL SERVICIO DESTINO''';    
    EXECUTE IMMEDIATE 'COMMENT ON COLUMN "PERSONALIZACIONES"."HOMOLOGACION_SERVICIOS"."OBSERVACION" IS ''OBSERVACION''';
	EXECUTE IMMEDIATE 'COMMENT ON TABLE "PERSONALIZACIONES"."HOMOLOGACION_SERVICIOS"  IS ''OBJETOS DE OPEN HOMOLOGADOS''';
  
    --- Aplica Permisos.
    BEGIN
      pkg_utilidades.prAplicarPermisos('HOMOLOGACION_SERVICIOS', 'PERSONALIZACIONES');
    END;
    --- Fin.
  END IF;
  CLOSE cuTabla;

END;
