DECLARE
  nuConta NUMBER;
BEGIN

 SELECT COUNT(*) INTO nuConta
 FROM dba_tables
 WHERE table_name = 'PROCESO_NEGOCIO' 
    AND OWNER = 'OPEN';
  
  IF nuConta = 0 THEN 
 
	 EXECUTE IMMEDIATE 'CREATE TABLE PROCESO_NEGOCIO (	CODIGO NUMBER(15,0) NOT NULL ENABLE, 
								DESCRIPCION VARCHAR2(400 BYTE)  NOT NULL, 
									CONSTRAINT PK_PROCESO_NEGOCIO PRIMARY KEY (CODIGO)) tablespace TSD_LOGS';

	EXECUTE IMMEDIATE q'#COMMENT ON COLUMN PROCESO_NEGOCIO.CODIGO IS 'CODIGO DEL PROCESO'#';
	EXECUTE IMMEDIATE q'#COMMENT ON COLUMN PROCESO_NEGOCIO.DESCRIPCION IS 'DESCRIPCION DEL PROCESO'#';
	EXECUTE IMMEDIATE q'#COMMENT ON TABLE PROCESO_NEGOCIO  IS 'PROCESOS DE NEGOCIO'#';

    EXECUTE IMMEDIATE  'GRANT UPDATE, INSERT, SELECT, DELETE ON PROCESO_NEGOCIO TO SYSTEM_OBJ_PRIVS_ROLE';

  END IF;

END;
