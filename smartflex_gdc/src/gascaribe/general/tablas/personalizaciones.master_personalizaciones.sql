DECLARE
	nuExiste 	NUMBER;
	cursor cuDatos is
	select count(1)
	  from dba_tab_columns
	 where table_name='MASTER_PERSONALIZACIONES'
	   and column_name='COMENTARIO';
BEGIN
	OPEN cuDatos;
	FETCH cuDatos INTO nuExiste;
	CLOSE cuDatos;

	IF nuExiste = 0 THEN
		EXECUTE IMMEDIATE 'ALTER TABLE PERSONALIZACIONES.MASTER_PERSONALIZACIONES ADD COMENTARIO VARCHAR2(100)';
		EXECUTE IMMEDIATE  q'#COMMENT ON COLUMN PERSONALIZACIONES.MASTER_PERSONALIZACIONES.COMENTARIO IS 'COMENTARIO'#';
	END IF;
END;
/