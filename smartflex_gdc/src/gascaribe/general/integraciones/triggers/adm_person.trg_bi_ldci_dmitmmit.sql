CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_LDCI_DMITMMIT
AFTER INSERT OR UPDATE OR DELETE ON LDCI_DMITMMIT
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

DMITMMIT NUMBER(9);
DMITCODI NUMBER(9);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  DMITMMIT := :new.DMITMMIT;
  DMITCODI := :new.DMITCODI;
  OPERATION := 'I';
ELSIF UPDATING THEN
  DMITMMIT := :new.DMITMMIT;
  DMITCODI := :new.DMITCODI;
  OPERATION := 'U';
ELSE
  DMITMMIT := :old.DMITMMIT;
  DMITCODI := :old.DMITCODI;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_LDCI_DMITMMIT (
  DMITMMIT,
  DMITCODI,
  OPERATION
)
VALUES (
  DMITMMIT,
  DMITCODI,
  OPERATION
);

EXCEPTION WHEN OTHERS THEN
    RAISE_APPLICATION_ERROR(
        -20000,
        'Error en TRG_BI_LDCI_DMITMMIT por -->' || sqlcode || chr(13) || sqlerrm
    );
END;
/
