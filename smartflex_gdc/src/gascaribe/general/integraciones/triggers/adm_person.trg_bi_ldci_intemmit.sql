CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_LDCI_INTEMMIT
AFTER INSERT OR UPDATE OR DELETE ON LDCI_INTEMMIT
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

MMITCODI NUMBER(9);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  MMITCODI := :new.MMITCODI;
  OPERATION := 'I';
ELSIF UPDATING THEN
  MMITCODI := :new.MMITCODI;
  OPERATION := 'U';
ELSE
  MMITCODI := :old.MMITCODI;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_LDCI_INTEMMIT (
  MMITCODI,
  OPERATION
)
VALUES (
  MMITCODI,
  OPERATION
);

EXCEPTION WHEN OTHERS THEN
    RAISE_APPLICATION_ERROR(
        -20000,
        'Error en TRG_BI_LDCI_INTEMMIT por -->' || sqlcode || chr(13) || sqlerrm
    );
END;
/
