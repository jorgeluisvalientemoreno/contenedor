CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_MO_ADDI_REQUEST_DATES 
AFTER INSERT OR UPDATE OR DELETE ON MO_ADDI_REQUEST_DATES
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

ADDI_REQU_DATE_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  ADDI_REQU_DATE_ID := :new.ADDI_REQU_DATE_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  ADDI_REQU_DATE_ID := :new.ADDI_REQU_DATE_ID;
  OPERATION := 'U';
ELSE
  ADDI_REQU_DATE_ID := :old.ADDI_REQU_DATE_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_MO_ADDI_REQUEST_DATES (
  ADDI_REQU_DATE_ID,
  OPERATION
)
VALUES (
  ADDI_REQU_DATE_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
