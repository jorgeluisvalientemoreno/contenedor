CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_GE_ITEMS 
AFTER INSERT OR UPDATE OR DELETE ON GE_ITEMS
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

ITEMS_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  ITEMS_ID := :new.ITEMS_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  ITEMS_ID := :new.ITEMS_ID;
  OPERATION := 'U';
ELSE
  ITEMS_ID := :old.ITEMS_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_GE_ITEMS (
  ITEMS_ID,
  OPERATION
)
VALUES (
  ITEMS_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
