CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_GE_LIST_UNITARY_COST 
AFTER INSERT OR UPDATE OR DELETE ON GE_LIST_UNITARY_COST
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

LIST_UNITARY_COST_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  LIST_UNITARY_COST_ID := :new.LIST_UNITARY_COST_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  LIST_UNITARY_COST_ID := :new.LIST_UNITARY_COST_ID;
  OPERATION := 'U';
ELSE
  LIST_UNITARY_COST_ID := :old.LIST_UNITARY_COST_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_GE_LIST_UNITARY_COST (
  LIST_UNITARY_COST_ID,
  OPERATION
)
VALUES (
  LIST_UNITARY_COST_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
