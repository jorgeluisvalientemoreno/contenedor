CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_OR_OPERATING_UNIT 
AFTER INSERT OR UPDATE OR DELETE ON OR_OPERATING_UNIT
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

OPERATING_UNIT_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  OPERATING_UNIT_ID := :new.OPERATING_UNIT_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  OPERATING_UNIT_ID := :new.OPERATING_UNIT_ID;
  OPERATION := 'U';
ELSE
  OPERATING_UNIT_ID := :old.OPERATING_UNIT_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_OR_OPERATING_UNIT (
  OPERATING_UNIT_ID,
  OPERATION
)
VALUES (
  OPERATING_UNIT_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
