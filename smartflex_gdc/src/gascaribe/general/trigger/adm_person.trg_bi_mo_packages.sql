CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_MO_PACKAGES 
AFTER INSERT OR UPDATE OR DELETE ON MO_PACKAGES
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

PACKAGE_ID NUMBER(15);
IS_DELETE VARCHAR(1);

BEGIN

IF DELETING THEN
  PACKAGE_ID := :old.PACKAGE_ID;
  IS_DELETE := 'Y';
ELSE
  PACKAGE_ID := :new.PACKAGE_ID;
  IS_DELETE := 'N';
END IF;

INSERT INTO LDCBI_MO_PACKAGES (
  PACKAGE_ID,
  IS_DELETE
)
VALUES (
  PACKAGE_ID,
  IS_DELETE
);

EXCEPTION
    WHEN OTHERS THEN
        RAISE EX.CONTROLLED_ERROR;
END;
/
