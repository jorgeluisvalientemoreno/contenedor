CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_GE_ITEMS_SERIADO 
AFTER INSERT OR UPDATE OR DELETE ON GE_ITEMS_SERIADO
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

ID_ITEMS_SERIADO NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  ID_ITEMS_SERIADO := :new.ID_ITEMS_SERIADO;
  OPERATION := 'I';
ELSIF UPDATING THEN
  ID_ITEMS_SERIADO := :new.ID_ITEMS_SERIADO;
  OPERATION := 'U';
ELSE
  ID_ITEMS_SERIADO := :old.ID_ITEMS_SERIADO;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_GE_ITEMS_SERIADO (
  ID_ITEMS_SERIADO,
  OPERATION
)
VALUES (
  ID_ITEMS_SERIADO,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
