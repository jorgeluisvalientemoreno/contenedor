CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_PR_SUBS_TYPE_PROD AFTER INSERT OR UPDATE OR DELETE ON PR_SUBS_TYPE_PROD
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

SUBS_TYPE_PROD_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  SUBS_TYPE_PROD_ID := :new.SUBS_TYPE_PROD_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  SUBS_TYPE_PROD_ID := :new.SUBS_TYPE_PROD_ID;
  OPERATION := 'U';
ELSE
  SUBS_TYPE_PROD_ID := :old.SUBS_TYPE_PROD_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_PR_SUBS_TYPE_PROD (
  SUBS_TYPE_PROD_ID,
  OPERATION
)
VALUES (
  SUBS_TYPE_PROD_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
