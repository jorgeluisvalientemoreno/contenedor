CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_AB_SEGMENTS 
AFTER INSERT OR UPDATE OR DELETE ON AB_SEGMENTS
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

SEGMENTS_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  SEGMENTS_ID := :new.SEGMENTS_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  SEGMENTS_ID := :new.SEGMENTS_ID;
  OPERATION := 'U';
ELSE
  SEGMENTS_ID := :old.SEGMENTS_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_AB_SEGMENTS (
  SEGMENTS_ID,
  OPERATION
)
VALUES (
  SEGMENTS_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE_APPLICATION_ERROR(-20000,
                            'Error en TRG_BI_AB_SEGMENTS por -->' || sqlcode ||
                            chr(13) || sqlerrm);
END;
/
