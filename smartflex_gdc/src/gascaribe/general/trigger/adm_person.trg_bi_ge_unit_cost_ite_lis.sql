CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_GE_UNIT_COST_ITE_LIS 
AFTER INSERT OR UPDATE OR DELETE ON GE_UNIT_COST_ITE_LIS
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

LIST_UNITARY_COST_ID NUMBER(15);
ITEMS_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  LIST_UNITARY_COST_ID := :new.LIST_UNITARY_COST_ID;
  ITEMS_ID := :new.ITEMS_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  LIST_UNITARY_COST_ID := :new.LIST_UNITARY_COST_ID;
  ITEMS_ID := :new.ITEMS_ID;
  OPERATION := 'U';
ELSE
  LIST_UNITARY_COST_ID := :old.LIST_UNITARY_COST_ID;
  ITEMS_ID := :old.ITEMS_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_GE_UNIT_COST_ITE_LIS (
  LIST_UNITARY_COST_ID,
  ITEMS_ID,
  OPERATION
)
VALUES (
  LIST_UNITARY_COST_ID,
  ITEMS_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
