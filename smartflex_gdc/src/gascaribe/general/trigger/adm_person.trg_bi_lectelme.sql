CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_LECTELME 
AFTER INSERT OR UPDATE OR DELETE ON LECTELME
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

LEEMCONS NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  LEEMCONS := :new.LEEMCONS;
  OPERATION := 'I';
ELSIF UPDATING THEN
  LEEMCONS := :new.LEEMCONS;
  OPERATION := 'U';
ELSE
  LEEMCONS := :old.LEEMCONS;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_LECTELME (
  LEEMCONS,
  OPERATION
)
VALUES (
  LEEMCONS,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
