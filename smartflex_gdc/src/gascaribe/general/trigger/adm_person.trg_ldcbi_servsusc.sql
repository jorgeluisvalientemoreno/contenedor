CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_LDCBI_SERVSUSC
AFTER INSERT OR UPDATE OR DELETE ON OPEN.SERVSUSC

REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE
SESUNUSE NUMBER(10);
IS_DELETE VARCHAR(1);

BEGIN

IF DELETING THEN
  SESUNUSE := :old.SESUNUSE;
  IS_DELETE := 'Y';
ELSE
  SESUNUSE := :new.SESUNUSE;
  IS_DELETE := 'N';
END IF;

INSERT INTO OPEN.LDCBI_SERVSUSC (
  SESUNUSE,
  IS_DELETE
)
VALUES (
  SESUNUSE,
  IS_DELETE
);

EXCEPTION
    WHEN OTHERS THEN
        RAISE EX.CONTROLLED_ERROR;
END;
/
