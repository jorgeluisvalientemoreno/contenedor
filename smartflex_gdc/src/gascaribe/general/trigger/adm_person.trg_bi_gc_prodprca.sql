CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_GC_PRODPRCA 
AFTER INSERT OR UPDATE OR DELETE ON GC_PRODPRCA
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

PRPCCONS NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  PRPCCONS := :new.PRPCCONS;
  OPERATION := 'I';
ELSIF UPDATING THEN
  PRPCCONS := :new.PRPCCONS;
  OPERATION := 'U';
ELSE
  PRPCCONS := :old.PRPCCONS;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_GC_PRODPRCA (
  PRPCCONS,
  OPERATION
)
VALUES (
  PRPCCONS,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
