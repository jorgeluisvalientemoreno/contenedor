CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_LDC_SEGUROVOLUNTARIO
AFTER INSERT OR UPDATE OR DELETE ON LDC_SEGUROVOLUNTARIO
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

SEGUROVOLUNTARIO_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  SEGUROVOLUNTARIO_ID := :new.SEGUROVOLUNTARIO_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  SEGUROVOLUNTARIO_ID := :new.SEGUROVOLUNTARIO_ID;
  OPERATION := 'U';
ELSE
  SEGUROVOLUNTARIO_ID := :old.SEGUROVOLUNTARIO_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_LDC_SEGUROVOLUNTARIO (
  SEGUROVOLUNTARIO_ID,
  OPERATION
)
VALUES (
  SEGUROVOLUNTARIO_ID,
  OPERATION
);

EXCEPTION WHEN OTHERS THEN
    RAISE_APPLICATION_ERROR(
        -20000,
        'Error en TRG_BI_LDC_SEGUROVOLUNTARIO por -->' || sqlcode || chr(13) || sqlerrm
    );
END;
/
