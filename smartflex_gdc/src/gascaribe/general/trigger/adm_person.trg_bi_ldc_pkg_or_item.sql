CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_LDC_PKG_OR_ITEM AFTER INSERT OR UPDATE OR DELETE ON LDC_PKG_OR_ITEM
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

LDC_PKG_OR_ITEM_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  LDC_PKG_OR_ITEM_ID := :new.LDC_PKG_OR_ITEM_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  LDC_PKG_OR_ITEM_ID := :new.LDC_PKG_OR_ITEM_ID;
  OPERATION := 'U';
ELSE
  LDC_PKG_OR_ITEM_ID := :old.LDC_PKG_OR_ITEM_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_LDC_PKG_OR_ITEM (
  LDC_PKG_OR_ITEM_ID,
  OPERATION
)
VALUES (
  LDC_PKG_OR_ITEM_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
