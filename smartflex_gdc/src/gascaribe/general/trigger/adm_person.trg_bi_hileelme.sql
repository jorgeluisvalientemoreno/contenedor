CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_HILEELME 
AFTER INSERT OR UPDATE OR DELETE ON HILEELME
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

HLEMCONS NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  HLEMCONS := :new.HLEMCONS;
  OPERATION := 'I';
ELSIF UPDATING THEN
  HLEMCONS := :new.HLEMCONS;
  OPERATION := 'U';
ELSE
  HLEMCONS := :old.HLEMCONS;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_HILEELME (
  HLEMCONS,
  OPERATION
)
VALUES (
  HLEMCONS,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
