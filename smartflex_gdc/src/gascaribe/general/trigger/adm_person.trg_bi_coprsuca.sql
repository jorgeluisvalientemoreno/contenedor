CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_COPRSUCA 
AFTER INSERT OR UPDATE OR DELETE ON COPRSUCA
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

CPSCCONS NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  CPSCCONS := :new.CPSCCONS;
  OPERATION := 'I';
ELSIF UPDATING THEN
  CPSCCONS := :new.CPSCCONS;
  OPERATION := 'U';
ELSE
  CPSCCONS := :old.CPSCCONS;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_COPRSUCA (
  CPSCCONS,
  OPERATION
)
VALUES (
  CPSCCONS,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
