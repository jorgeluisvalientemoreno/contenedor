CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_COMPSESU 
AFTER INSERT OR UPDATE OR DELETE ON COMPSESU
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

CMSSIDCO NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  CMSSIDCO := :new.CMSSIDCO;
  OPERATION := 'I';
ELSIF UPDATING THEN
  CMSSIDCO := :new.CMSSIDCO;
  OPERATION := 'U';
ELSE
  CMSSIDCO := :old.CMSSIDCO;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_COMPSESU (
  CMSSIDCO,
  OPERATION
)
VALUES (
  CMSSIDCO,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
