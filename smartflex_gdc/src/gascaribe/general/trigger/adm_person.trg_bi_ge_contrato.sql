CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_GE_CONTRATO 
AFTER INSERT OR UPDATE OR DELETE ON GE_CONTRATO
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

ID_CONTRATO NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  ID_CONTRATO := :new.ID_CONTRATO;
  OPERATION := 'I';
ELSIF UPDATING THEN
  ID_CONTRATO := :new.ID_CONTRATO;
  OPERATION := 'U';
ELSE
  ID_CONTRATO := :old.ID_CONTRATO;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_GE_CONTRATO (
  ID_CONTRATO,
  OPERATION
)
VALUES (
  ID_CONTRATO,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
