CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_PR_PRODUCT_REQUEST AFTER INSERT OR UPDATE OR DELETE ON PR_PRODUCT_REQUEST
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

PRODUCT_REQUEST_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  PRODUCT_REQUEST_ID := :new.PRODUCT_REQUEST_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  PRODUCT_REQUEST_ID := :new.PRODUCT_REQUEST_ID;
  OPERATION := 'U';
ELSE
  PRODUCT_REQUEST_ID := :old.PRODUCT_REQUEST_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_PR_PRODUCT_REQUEST (
  PRODUCT_REQUEST_ID,
  OPERATION
)
VALUES (
  PRODUCT_REQUEST_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
