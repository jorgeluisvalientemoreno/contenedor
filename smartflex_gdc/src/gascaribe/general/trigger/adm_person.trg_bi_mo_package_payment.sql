CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_MO_PACKAGE_PAYMENT 
AFTER INSERT OR UPDATE OR DELETE ON MO_PACKAGE_PAYMENT
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

PACKAGE_PAYMENT_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  PACKAGE_PAYMENT_ID := :new.PACKAGE_PAYMENT_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  PACKAGE_PAYMENT_ID := :new.PACKAGE_PAYMENT_ID;
  OPERATION := 'U';
ELSE
  PACKAGE_PAYMENT_ID := :old.PACKAGE_PAYMENT_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_MO_PACKAGE_PAYMENT (
  PACKAGE_PAYMENT_ID,
  OPERATION
)
VALUES (
  PACKAGE_PAYMENT_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
