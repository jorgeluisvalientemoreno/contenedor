CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_GE_ITEMS_DOCUMENTO
AFTER INSERT OR UPDATE OR DELETE ON GE_ITEMS_DOCUMENTO
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

ID_ITEMS_DOCUMENTO NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  ID_ITEMS_DOCUMENTO := :new.ID_ITEMS_DOCUMENTO;
  OPERATION := 'I';
ELSIF UPDATING THEN
  ID_ITEMS_DOCUMENTO := :new.ID_ITEMS_DOCUMENTO;
  OPERATION := 'U';
ELSE
  ID_ITEMS_DOCUMENTO := :old.ID_ITEMS_DOCUMENTO;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_GE_ITEMS_DOCUMENTO (
  ID_ITEMS_DOCUMENTO,
  OPERATION
)
VALUES (
  ID_ITEMS_DOCUMENTO,
  OPERATION
);

EXCEPTION WHEN OTHERS THEN
    RAISE_APPLICATION_ERROR(
        -20000,
        'Error en TRG_BI_GE_ITEMS_DOCUMENTO por -->' || sqlcode || chr(13) || sqlerrm
    );
END;
/
