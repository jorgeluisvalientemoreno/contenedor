CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_GE_ITEMS_DOC_REL
AFTER INSERT OR UPDATE OR DELETE ON GE_ITEMS_DOC_REL
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

ID_ITEMS_DOC_RELACION NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  ID_ITEMS_DOC_RELACION := :new.ID_ITEMS_DOC_RELACION;
  OPERATION := 'I';
ELSIF UPDATING THEN
  ID_ITEMS_DOC_RELACION := :new.ID_ITEMS_DOC_RELACION;
  OPERATION := 'U';
ELSE
  ID_ITEMS_DOC_RELACION := :old.ID_ITEMS_DOC_RELACION;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_GE_ITEMS_DOC_REL (
  ID_ITEMS_DOC_RELACION,
  OPERATION
)
VALUES (
  ID_ITEMS_DOC_RELACION,
  OPERATION
);

EXCEPTION WHEN OTHERS THEN
    RAISE_APPLICATION_ERROR(
        -20000,
        'Error en TRG_BI_GE_ITEMS_DOC_REL por -->' || sqlcode || chr(13) || sqlerrm
    );
END;
/
