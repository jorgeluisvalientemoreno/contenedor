CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_OR_ORDER_ACT_VAR_DET 
AFTER INSERT OR UPDATE OR DELETE ON OR_ORDER_ACT_VAR_DET
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

ORDER_ACT_VAR_DET_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  ORDER_ACT_VAR_DET_ID := :new.ORDER_ACT_VAR_DET_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  ORDER_ACT_VAR_DET_ID := :new.ORDER_ACT_VAR_DET_ID;
  OPERATION := 'U';
ELSE
  ORDER_ACT_VAR_DET_ID := :old.ORDER_ACT_VAR_DET_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_OR_ORDER_ACT_VAR_DET (
  ORDER_ACT_VAR_DET_ID,
  OPERATION
)
VALUES (
  ORDER_ACT_VAR_DET_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
