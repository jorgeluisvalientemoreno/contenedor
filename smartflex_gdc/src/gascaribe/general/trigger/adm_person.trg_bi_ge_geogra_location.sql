CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_GE_GEOGRA_LOCATION 
AFTER INSERT OR UPDATE OR DELETE ON GE_GEOGRA_LOCATION
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

GEOGRAP_LOCATION_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  GEOGRAP_LOCATION_ID := :new.GEOGRAP_LOCATION_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  GEOGRAP_LOCATION_ID := :new.GEOGRAP_LOCATION_ID;
  OPERATION := 'U';
ELSE
  GEOGRAP_LOCATION_ID := :old.GEOGRAP_LOCATION_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_GE_GEOGRA_LOCATION (
  GEOGRAP_LOCATION_ID,
  OPERATION
)
VALUES (
  GEOGRAP_LOCATION_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
