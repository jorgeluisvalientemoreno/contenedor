CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_CT_ORDER_CERTIFICA 
AFTER INSERT OR UPDATE OR DELETE ON CT_ORDER_CERTIFICA
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

ORDER_ID NUMBER(15);
CERTIFICATE_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  ORDER_ID := :new.ORDER_ID;
  CERTIFICATE_ID := :new.CERTIFICATE_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  ORDER_ID := :new.ORDER_ID;
  CERTIFICATE_ID := :new.CERTIFICATE_ID;
  OPERATION := 'U';
ELSE
  ORDER_ID := :old.ORDER_ID;
  CERTIFICATE_ID := :old.CERTIFICATE_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_CT_ORDER_CERTIFICA (
  ORDER_ID,
  CERTIFICATE_ID,
  OPERATION
)
VALUES (
  ORDER_ID,
  CERTIFICATE_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
