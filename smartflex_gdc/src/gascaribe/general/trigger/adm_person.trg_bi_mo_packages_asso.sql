CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_MO_PACKAGES_ASSO 
AFTER INSERT OR UPDATE OR DELETE ON MO_PACKAGES_ASSO
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

PACKAGES_ASSO_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  PACKAGES_ASSO_ID := :new.PACKAGES_ASSO_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  PACKAGES_ASSO_ID := :new.PACKAGES_ASSO_ID;
  OPERATION := 'U';
ELSE
  PACKAGES_ASSO_ID := :old.PACKAGES_ASSO_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_MO_PACKAGES_ASSO (
  PACKAGES_ASSO_ID,
  OPERATION
)
VALUES (
  PACKAGES_ASSO_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
