CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_LDC_PLAZOS_CERT AFTER INSERT OR UPDATE OR DELETE ON LDC_PLAZOS_CERT
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

PLAZOS_CERT_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  PLAZOS_CERT_ID := :new.PLAZOS_CERT_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  PLAZOS_CERT_ID := :new.PLAZOS_CERT_ID;
  OPERATION := 'U';
ELSE
  PLAZOS_CERT_ID := :old.PLAZOS_CERT_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_LDC_PLAZOS_CERT (
  PLAZOS_CERT_ID,
  OPERATION
)
VALUES (
  PLAZOS_CERT_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
