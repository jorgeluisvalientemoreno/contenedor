CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_LDC_PROD_COM_SECTOR AFTER INSERT OR UPDATE OR DELETE ON LDC_PROD_COMERC_SECTOR
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

PRODUCT_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  PRODUCT_ID := :new.PRODUCT_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  PRODUCT_ID := :new.PRODUCT_ID;
  OPERATION := 'U';
ELSE
  PRODUCT_ID := :old.PRODUCT_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_LDC_PROD_COMERC_SECTOR (
  PRODUCT_ID,
  OPERATION
)
VALUES (
  PRODUCT_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE_APPLICATION_ERROR(-20000,
                            'Error en TRG_BI_LDC_PROD_COM_SECTOR por -->' || sqlcode ||
                            chr(13) || sqlerrm);
END;
/
