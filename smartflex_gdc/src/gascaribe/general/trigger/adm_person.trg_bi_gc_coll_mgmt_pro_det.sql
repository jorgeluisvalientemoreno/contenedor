CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_GC_COLL_MGMT_PRO_DET 
AFTER INSERT OR UPDATE OR DELETE ON GC_COLL_MGMT_PRO_DET
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

COLL_MGMT_PRO_DET_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  COLL_MGMT_PRO_DET_ID := :new.COLL_MGMT_PRO_DET_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  COLL_MGMT_PRO_DET_ID := :new.COLL_MGMT_PRO_DET_ID;
  OPERATION := 'U';
ELSE
  COLL_MGMT_PRO_DET_ID := :old.COLL_MGMT_PRO_DET_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_GC_COLL_MGMT_PRO_DET (
  COLL_MGMT_PRO_DET_ID,
  OPERATION
)
VALUES (
  COLL_MGMT_PRO_DET_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
