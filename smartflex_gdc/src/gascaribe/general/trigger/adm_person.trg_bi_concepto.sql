CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_CONCEPTO 
AFTER INSERT OR UPDATE OR DELETE ON CONCEPTO
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

CONCCODI NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  CONCCODI := :new.CONCCODI;
  OPERATION := 'I';
ELSIF UPDATING THEN
  CONCCODI := :new.CONCCODI;
  OPERATION := 'U';
ELSE
  CONCCODI := :old.CONCCODI;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_CONCEPTO (
  CONCCODI,
  OPERATION
)
VALUES (
  CONCCODI,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
