CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_DIFERIDO 
AFTER INSERT OR UPDATE OR DELETE ON DIFERIDO
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

DIFECODI NUMBER(15);
IS_DELETE VARCHAR(1);

BEGIN

IF DELETING THEN
  DIFECODI := :old.DIFECODI;
  IS_DELETE := 'Y';
ELSE
  DIFECODI := :new.DIFECODI;
  IS_DELETE := 'N';
END IF;

INSERT INTO LDCBI_DIFERIDO (
  DIFECODI,
  IS_DELETE
)
VALUES (
  DIFECODI,
  IS_DELETE
);

EXCEPTION
    WHEN OTHERS THEN
        RAISE EX.CONTROLLED_ERROR;
END;
/
