CREATE OR REPLACE TRIGGER ADM_PERSON.TGR_LDC_CONF_ENGI_ACTI
  AFTER INSERT OR DELETE ON PS_ENGINEERING_ACTIV
  FOR EACH ROW
BEGIN
  IF INSERTING then

     INSERT INTO LDC_CONF_ENGI_ACTI(ITEMS_ID, PRODUCT_ONLY) VALUES(:new.ITEMS_ID, 'N');

  ELSE

     DELETE FROM LDC_CONF_ENGI_ACTI WHERE ITEMS_ID = :old.ITEMS_ID;

  END IF;

END TGR_LDC_CONF_ENGI_ACTI;
/
