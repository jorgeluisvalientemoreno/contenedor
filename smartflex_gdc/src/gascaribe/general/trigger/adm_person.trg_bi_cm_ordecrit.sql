CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_CM_ORDECRIT 
AFTER INSERT OR UPDATE OR DELETE ON CM_ORDECRIT
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

ORCRORDE NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  ORCRORDE := :new.ORCRORDE;
  OPERATION := 'I';
ELSIF UPDATING THEN
  ORCRORDE := :new.ORCRORDE;
  OPERATION := 'U';
ELSE
  ORCRORDE := :old.ORCRORDE;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_CM_ORDECRIT (
  ORCRORDE,
  OPERATION
)
VALUES (
  ORCRORDE,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
