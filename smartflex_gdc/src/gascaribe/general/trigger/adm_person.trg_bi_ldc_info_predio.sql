CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_LDC_INFO_PREDIO AFTER INSERT OR UPDATE OR DELETE ON LDC_INFO_PREDIO
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

LDC_INFO_PREDIO_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  LDC_INFO_PREDIO_ID := :new.LDC_INFO_PREDIO_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  LDC_INFO_PREDIO_ID := :new.LDC_INFO_PREDIO_ID;
  OPERATION := 'U';
ELSE
  LDC_INFO_PREDIO_ID := :old.LDC_INFO_PREDIO_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_LDC_INFO_PREDIO (
  LDC_INFO_PREDIO_ID,
  OPERATION
)
VALUES (
  LDC_INFO_PREDIO_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE_APPLICATION_ERROR(-20000,
                            'Error en TRG_BI_LDC_INFO_PREDIO por -->' || sqlcode ||
                            chr(13) || sqlerrm);
END;
/
