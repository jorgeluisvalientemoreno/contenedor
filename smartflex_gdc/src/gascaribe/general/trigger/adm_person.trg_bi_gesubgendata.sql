CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_GESUBGENDATA 
AFTER INSERT OR UPDATE OR DELETE ON GE_SUBS_GENERAL_DATA
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

SUBSCRIBER_ID NUMBER(15);
IS_DELETE VARCHAR(1);

BEGIN

IF DELETING THEN
  SUBSCRIBER_ID := :old.SUBSCRIBER_ID;
  IS_DELETE := 'Y';
ELSE
  SUBSCRIBER_ID := :new.SUBSCRIBER_ID;
  IS_DELETE := 'N';
END IF;

INSERT INTO LDCBI_GE_SUBS_GENERAL_DATA (
  SUBSCRIBER_ID,
  IS_DELETE
)
VALUES (
  SUBSCRIBER_ID,
  IS_DELETE
);

EXCEPTION
    WHEN OTHERS THEN
        RAISE EX.CONTROLLED_ERROR;
END;
/
