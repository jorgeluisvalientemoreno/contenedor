CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_CICLO 
AFTER INSERT OR UPDATE OR DELETE ON CICLO
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

CICLCODI NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  CICLCODI := :new.CICLCODI;
  OPERATION := 'I';
ELSIF UPDATING THEN
  CICLCODI := :new.CICLCODI;
  OPERATION := 'U';
ELSE
  CICLCODI := :old.CICLCODI;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_CICLO (
  CICLCODI,
  OPERATION
)
VALUES (
  CICLCODI,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
