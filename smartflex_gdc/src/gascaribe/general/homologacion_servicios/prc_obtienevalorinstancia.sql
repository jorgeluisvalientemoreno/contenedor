BEGIN
	MERGE INTO PERSONALIZACIONES.HOMOLOGACION_SERVICIOS A USING
		(
			SELECT 	'OPEN' as ESQUEMA_ORIGEN,
					'GE_BOINSTANCECONTROL.GETATTRIBUTENEWVALUE' as SERVICIO_ORIGEN,
					'Obtiene el valor de la instancia' as DESCRIPCION_ORIGEN,
					'ADM_PERSON' as ESQUEMA_DESTINO,
					'PRC_OBTIENEVALORINSTANCIA' as SERVICIO_DESTINO,
					'Obtiene el valor de la instancia' as DESCRIPCION_DESTINO
			FROM 	DUAL) B
			ON (A.ESQUEMA_ORIGEN = B.ESQUEMA_ORIGEN and A.SERVICIO_ORIGEN = B.SERVICIO_ORIGEN)
			WHEN NOT MATCHED THEN 
				INSERT (ESQUEMA_ORIGEN, SERVICIO_ORIGEN, DESCRIPCION_ORIGEN, ESQUEMA_DESTINO, SERVICIO_DESTINO, DESCRIPCION_DESTINO)
				VALUES (B.ESQUEMA_ORIGEN, B.SERVICIO_ORIGEN, B.DESCRIPCION_ORIGEN, B.ESQUEMA_DESTINO, B.SERVICIO_DESTINO, 
						B.DESCRIPCION_DESTINO)
			WHEN MATCHED THEN
				UPDATE SET 	A.DESCRIPCION_ORIGEN = B.DESCRIPCION_ORIGEN,
							A.ESQUEMA_DESTINO = B.ESQUEMA_DESTINO,
							A.SERVICIO_DESTINO = B.SERVICIO_DESTINO,
							A.DESCRIPCION_DESTINO = B.DESCRIPCION_DESTINO;

	COMMIT;

END;
