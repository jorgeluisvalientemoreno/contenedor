MERGE INTO OPEN.PARAMETROS A USING
 (SELECT
  'TIPO_SOLICITUD_FLAG_VALIDATE' as CODIGO,
  'Tipos de solicitud para el atributo FLAG_VALIDATE del flujo de interacci√≥n' as DESCRIPCION,
  NULL as VALOR_NUMERICO,
  '100030' as VALOR_CADENA,
  NULL as VALOR_FECHA,
  20 as PROCESO, 
  'A' as ESTADO,
  'N' as OBLIGATORIO,
  SYSDATE as FECHA_CREACION,
  SYSDATE as FECHA_ACTUALIZACION,
  'OPEN' as USUARIO,
  'NO TERMINAL' as TERMINAL
  FROM DUAL) B
ON (A.CODIGO = B.CODIGO)
WHEN NOT MATCHED THEN 
