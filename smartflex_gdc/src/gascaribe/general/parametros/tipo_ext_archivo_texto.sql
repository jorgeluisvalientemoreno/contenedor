Prompt Creando el parametro TIPO_EXT_ARCHIVO_TEXTO
BEGIN
    MERGE INTO PERSONALIZACIONES.PARAMETROS A USING
     (SELECT
      'TIPO_EXT_ARCHIVO_TEXTO' as CODIGO,
      'Tipo de Extensión para archivos de tipo Texto, permitidos para la gestión de archivos' as DESCRIPCION,
      NULL as VALOR_NUMERICO,
      '.txt|.csv|.log|.sql|.json|.xml|.html' as VALOR_CADENA,
      NULL as VALOR_FECHA,
      NULL as PROCESO,
      TO_DATE('15/04/2024 08:32:00', 'DD/MM/YYYY HH24:MI:SS') as FECHA_CREACION,
      TO_DATE('28/05/2024 11:22:00', 'DD/MM/YYYY HH24:MI:SS') as FECHA_ACTUALIZACION,
      'OPEN' as USUARIO,
      NULL as TERMINAL
      FROM DUAL) B
    ON (A.CODIGO = B.CODIGO)
    WHEN NOT MATCHED THEN 
    INSERT (
      CODIGO, DESCRIPCION, VALOR_NUMERICO, VALOR_CADENA, VALOR_FECHA, 
      PROCESO, FECHA_CREACION, FECHA_ACTUALIZACION, USUARIO, TERMINAL)
    VALUES (
      B.CODIGO, B.DESCRIPCION, B.VALOR_NUMERICO, B.VALOR_CADENA, B.VALOR_FECHA, 
      B.PROCESO, B.FECHA_CREACION, B.FECHA_ACTUALIZACION, B.USUARIO, B.TERMINAL)
    WHEN MATCHED THEN
    UPDATE SET 
      A.DESCRIPCION = B.DESCRIPCION,
      A.VALOR_NUMERICO = B.VALOR_NUMERICO,
      A.VALOR_CADENA = B.VALOR_CADENA,
      A.VALOR_FECHA = B.VALOR_FECHA,
      A.PROCESO = B.PROCESO,
      A.FECHA_CREACION = B.FECHA_CREACION,
      A.FECHA_ACTUALIZACION = B.FECHA_ACTUALIZACION,
      A.USUARIO = B.USUARIO,
      A.TERMINAL = B.TERMINAL;

    COMMIT;
END;
