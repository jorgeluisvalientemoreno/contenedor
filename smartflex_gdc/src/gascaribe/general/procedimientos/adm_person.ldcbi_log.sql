CREATE OR REPLACE PROCEDURE ADM_PERSON.LDCBI_LOG (
  INPROCESS IN VARCHAR2,
  INNUMERIC_VALUE IN NUMBER,
  INSTRING_VALUE  IN VARCHAR2,
  INERROR_CODE  IN NUMBER,
  INOBSERVATION IN VARCHAR2
) IS
  PRAGMA AUTONOMOUS_TRANSACTION;
BEGIN
  INSERT INTO LDCBI_LOG_TRACE ( LOG_TRACE_ID, PROCESS, NUMERIC_VALUE, STRING_VALUE, ERROR_CODE, OBSERVATION )
  VALUES ( LDCBI_LOG_TRACE_seq.nextval, INPROCESS, INNUMERIC_VALUE, INSTRING_VALUE, INERROR_CODE, INOBSERVATION ) ;
  COMMIT;
END;
/
BEGIN
    pkg_utilidades.prAplicarPermisos('LDCBI_LOG', 'ADM_PERSON');
END;
/
