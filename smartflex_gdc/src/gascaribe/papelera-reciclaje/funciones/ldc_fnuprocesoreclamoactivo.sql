DECLARE
  nuConta NUMBER;
BEGIN
  SELECT COUNT(*)
    INTO nuConta
    FROM dba_objects
   WHERE upper(object_name) = 'LDC_FNUPROCESORECLAMOACTIVO'
     AND OWNER = 'OPEN'
     AND OBJECT_TYPE <> 'SYNONYM';

  IF nuConta > 0 then
    EXECUTE IMMEDIATE 'DROP FUNCTION LDC_FNUPROCESORECLAMOACTIVO';
  END IF;
END;
/
