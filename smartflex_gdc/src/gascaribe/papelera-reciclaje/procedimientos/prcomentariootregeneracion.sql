set serveroutput on;
PROMPT BORRAR PROCEDIMIENTO PRCOMENTARIOOTREGENERACION
DECLARE
  nuConta NUMBER;
BEGIN
  SELECT COUNT(*) INTO nuConta
  FROM dba_objects
  WHERE object_name = 'PRCOMENTARIOOTREGENERACION'
   AND OWNER = 'OPEN'
   AND OBJECT_TYPE <> 'SYNONYM';
   
  IF nuConta > 0 THEN
    EXECUTE IMMEDIATE 'DROP PROCEDURE PRCOMENTARIOOTREGENERACION';
    dbms_output.put_line('PROCEDURE PRCOMENTARIOOTREGENERACION BORRADO');
  END IF;
EXCEPTION
    WHEN OTHERS THEN 
        dbms_output.put_line('No se pudo borrar el procedimiento PRCOMENTARIOOTREGENERACION, '||sqlerrm); 
END;
/