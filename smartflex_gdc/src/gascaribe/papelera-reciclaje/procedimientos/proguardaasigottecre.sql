set serveroutput on;
PROMPT BORRAR PROCEDIMIENTO PROGUARDAASIGOTTECRE
DECLARE
  nuConta NUMBER;
BEGIN
  SELECT COUNT(*) INTO nuConta
  FROM dba_objects
  WHERE object_name = 'PROGUARDAASIGOTTECRE'
   AND OWNER = 'OPEN'
   AND OBJECT_TYPE <> 'SYNONYM';
   
  IF nuConta > 0 then
    EXECUTE IMMEDIATE 'DROP PROCEDURE PROGUARDAASIGOTTECRE';
    dbms_output.put_line('PROCEDURE PROGUARDAASIGOTTECRE BORRADO');
  END IF;
EXCEPTION
    WHEN OTHERS THEN 
        dbms_output.put_line('No se pudo borrar el procedimiento PROGUARDAASIGOTTECRE, '||sqlerrm); 
END;
/