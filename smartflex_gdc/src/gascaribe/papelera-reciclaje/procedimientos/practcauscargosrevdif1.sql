set serveroutput on;
PROMPT BORRAR PRACTCAUSCARGOSREVDIF1
DECLARE
  nuConta NUMBER;
BEGIN
  SELECT COUNT(*) INTO nuConta
  FROM dba_objects
  WHERE object_name = 'PRACTCAUSCARGOSREVDIF1'
   AND OWNER = 'OPEN'
   AND OBJECT_TYPE <> 'SYNONYM';
   
  IF nuConta > 0 then
    EXECUTE IMMEDIATE 'DROP PROCEDURE PRACTCAUSCARGOSREVDIF1';
  END IF;
EXCEPTION
    WHEN OTHERS THEN 
        dbms_output.put_line('No se pudo borrar procedimiento PRACTCAUSCARGOSREVDIF1, '||sqlerrm); 
END;
/