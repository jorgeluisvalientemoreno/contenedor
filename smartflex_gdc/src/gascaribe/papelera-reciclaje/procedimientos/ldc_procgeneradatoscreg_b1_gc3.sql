 DECLARE
  nuConta NUMBER;
BEGIN
SELECT COUNT(1) INTO nuConta
  FROM DBA_OBJECTS
  WHERE OBJECT_NAME = 'LDC_PROCGENERADATOSCREG_B1_GC3'
   AND OWNER = 'OPEN'
   AND OBJECT_TYPE='PROCEDURE';

  IF nuConta >= 1 THEN
      EXECUTE IMMEDIATE 'DROP PROCEDURE OPEN.LDC_PROCGENERADATOSCREG_B1_GC3';
  END IF;
END;
/