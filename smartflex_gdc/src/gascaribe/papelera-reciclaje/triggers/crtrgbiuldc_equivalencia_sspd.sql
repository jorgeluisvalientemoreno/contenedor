DECLARE
  nuConta NUMBER;
BEGIN

  SELECT COUNT(*) INTO nuConta
  FROM dba_objects
  WHERE object_name = 'CRTRGBIULDC_EQUIVALENCIA_SSPD'
   AND OWNER = 'OPEN'
   AND OBJECT_TYPE = 'TRIGGER';
   
  IF nuConta > 0 then
    EXECUTE IMMEDIATE 'DROP TRIGGER OPEN.CRTRGBIULDC_EQUIVALENCIA_SSPD';
  END IF;  
END;
/
