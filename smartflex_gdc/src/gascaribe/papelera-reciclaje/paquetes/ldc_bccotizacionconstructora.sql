DECLARE
 nuConta NUMBER;
BEGIN

 SELECT COUNT(*) INTO nuConta
 FROM dba_objects
 WHERE object_name = 'LDC_BCCOTIZACIONCONSTRUCTORA'
  AND OWNER = 'ADM_PERSON'
  AND OBJECT_TYPE = 'SYNONYM';
  
 IF nuConta > 0 then
  EXECUTE IMMEDIATE 'DROP SYNONYM ADM_PERSON.LDC_BCCOTIZACIONCONSTRUCTORA';
 END IF; 

 SELECT COUNT(*) INTO nuConta
 FROM dba_objects
 WHERE object_name = 'LDC_BCCOTIZACIONCONSTRUCTORA'
  AND OWNER = 'OPEN'
  AND OBJECT_TYPE = 'PACKAGE';
  
 IF nuConta > 0 then
  EXECUTE IMMEDIATE 'DROP PACKAGE OPEN.LDC_BCCOTIZACIONCONSTRUCTORA';
 END IF; 
END;
/
