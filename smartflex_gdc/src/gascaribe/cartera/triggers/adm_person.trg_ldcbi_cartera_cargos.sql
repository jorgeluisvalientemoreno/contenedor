CREATE OR REPLACE trigger ADM_PERSON.TRG_LDCBI_CARTERA_CARGOS
AFTER INSERT OR UPDATE OR DELETE ON CARGOS
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW
DECLARE
IDPRODUCTO NUMBER(15);
BEGIN
IF DELETING THEN
    IDPRODUCTO := :OLD.CARGNUSE ;
ELSE
    IDPRODUCTO := :NEW.CARGNUSE ;
END IF;
INSERT INTO LDCBI_PRODUCTO_TRG ( PRODUCT_ID ) VALUES ( IDPRODUCTO ) ;
END;
/
