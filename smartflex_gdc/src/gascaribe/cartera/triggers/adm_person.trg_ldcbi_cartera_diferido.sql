CREATE OR REPLACE trigger ADM_PERSON.TRG_LDCBI_CARTERA_DIFERIDO
AFTER INSERT OR UPDATE OR DELETE ON DIFERIDO
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW
DECLARE
IDPRODUCTO NUMBER(15);
BEGIN
IF DELETING THEN
    IDPRODUCTO := :OLD.DIFENUSE ;
ELSE
    IDPRODUCTO := :NEW.DIFENUSE ;
END IF;
INSERT INTO LDCBI_PRODUCTO_TRG ( PRODUCT_ID ) VALUES ( IDPRODUCTO ) ;
END;
/
