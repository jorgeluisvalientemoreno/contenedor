DECLARE
	nuConta	NUMBER;
BEGIN
	SELECT 	COUNT(1) 
	INTO 	nuConta
	FROM 	all_tab_columns
	WHERE 	table_name = 'LDC_AJUSTA_SUSPCONE'
	AND		column_name = 'MOTIVO';
  
	IF (nuConta = 0) THEN
		EXECUTE IMMEDIATE 'ALTER TABLE LDC_AJUSTA_SUSPCONE ADD MOTIVO VARCHAR2(200)';
		EXECUTE IMMEDIATE 'COMMENT ON COLUMN LDC_AJUSTA_SUSPCONE.MOTIVO IS ''MOTIVO''';
	END IF;
END;
