CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_PR_PROD_SUSPENSION AFTER INSERT OR UPDATE OR DELETE ON PR_PROD_SUSPENSION
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

PROD_SUSPENSION_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  PROD_SUSPENSION_ID := :new.PROD_SUSPENSION_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  PROD_SUSPENSION_ID := :new.PROD_SUSPENSION_ID;
  OPERATION := 'U';
ELSE
  PROD_SUSPENSION_ID := :old.PROD_SUSPENSION_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_PR_PROD_SUSPENSION (
  PROD_SUSPENSION_ID,
  OPERATION
)
VALUES (
  PROD_SUSPENSION_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
