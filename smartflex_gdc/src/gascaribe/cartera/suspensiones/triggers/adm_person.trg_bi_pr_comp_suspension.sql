CREATE OR REPLACE TRIGGER ADM_PERSON.TRG_BI_PR_COMP_SUSPENSION AFTER INSERT OR UPDATE OR DELETE ON PR_COMP_SUSPENSION
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

COMP_SUSPENSION_ID NUMBER(15);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  COMP_SUSPENSION_ID := :new.COMP_SUSPENSION_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  COMP_SUSPENSION_ID := :new.COMP_SUSPENSION_ID;
  OPERATION := 'U';
ELSE
  COMP_SUSPENSION_ID := :old.COMP_SUSPENSION_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_PR_COMP_SUSPENSION (
  COMP_SUSPENSION_ID,
  OPERATION
)
VALUES (
  COMP_SUSPENSION_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE EX.CONTROLLED_ERROR;
END;
/
