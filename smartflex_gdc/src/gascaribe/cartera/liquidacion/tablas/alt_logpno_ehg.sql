DECLARE
	nuConta number;
BEGIN	
	SELECT 	COUNT(1) 
	INTO 	nuConta
	FROM 	all_tab_columns
	WHERE 	table_name = 'LOGPNO_EHG'
	AND 	column_name = 'SESSION_ID';
	
	IF (nuConta = 0) THEN
		EXECUTE IMMEDIATE 'ALTER TABLE LOGPNO_EHG ADD SESSION_ID NUMBER(15)';
		
		EXECUTE IMMEDIATE 'CREATE INDEX IDX_LOGPNO_EHG_01 ON LOGPNO_EHG(SESSION_ID)';
		EXECUTE IMMEDIATE 'CREATE INDEX IDX_LOGPNO_EHG_02 ON LOGPNO_EHG(FECHA)';
	END IF;
  
END;
