DECLARE
	nuConta number;
BEGIN	
	SELECT 	COUNT(1) 
	INTO 	nuConta
	FROM 	all_tab_columns
	WHERE 	table_name = 'LDC_TMP_FPORDERSDATA'
	AND 	column_name = 'ACTIVITY_ID';
	
	IF (nuConta = 0) THEN
		EXECUTE IMMEDIATE 'ALTER TABLE LDC_TMP_FPORDERSDATA ADD ACTIVITY_ID NUMBER(15)';
	END IF;
	
	SELECT 	COUNT(1) 
	INTO 	nuConta
	FROM 	all_tab_columns
	WHERE 	table_name = 'LDC_TMP_FPORDERSDATA'
	AND 	column_name = 'FECHA_REGISTRO';
	
	IF (nuConta = 0) THEN
		EXECUTE IMMEDIATE 'ALTER TABLE LDC_TMP_FPORDERSDATA ADD FECHA_REGISTRO DATE';
		EXECUTE IMMEDIATE 'CREATE INDEX IDX_LDC_TMP_FPORDERSDATA_01 ON LDC_TMP_FPORDERSDATA(FECHA_REGISTRO)';
	END IF;
  
END;
