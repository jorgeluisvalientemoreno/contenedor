CREATE OR REPLACE TRIGGER adm_person.TRG_DE_LDC_PROTECCION_DATOS AFTER INSERT OR UPDATE OR DELETE ON OPEN.LDC_PROTECCION_DATOS
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW

DECLARE

PROTECCION_DATOS_ID NUMBER(20);
OPERATION VARCHAR2(1);

BEGIN

IF INSERTING THEN
  PROTECCION_DATOS_ID := :new.PROTECCION_DATOS_ID;
  OPERATION := 'I';
ELSIF UPDATING THEN
  PROTECCION_DATOS_ID := :old.PROTECCION_DATOS_ID;
  OPERATION := 'U';
ELSE
  PROTECCION_DATOS_ID := :old.PROTECCION_DATOS_ID;
  OPERATION := 'D';
END IF;

INSERT INTO LDCBI_LDC_PROTECCION_DATOS (
  PROTECCION_DATOS_ID,
  OPERATION
)
VALUES (
  PROTECCION_DATOS_ID,
  OPERATION
);

EXCEPTION
  WHEN OTHERS THEN
    RAISE_APPLICATION_ERROR(-20000,
                            'Error en TRG_DE_LDC_PROTECCION_DATOS por -->' || sqlcode ||
                            chr(13) || sqlerrm);
END;
