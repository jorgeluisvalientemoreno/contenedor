CREATE OR REPLACE TRIGGER ADM_PERSON.LDC_TRG_BIU_VALIDFLAG
  BEFORE INSERT OR UPDATE ON GC_DEBT_NEGOT_PROD 
  FOR EACH ROW
DECLARE
    nuError     number;
    sbError     varchar2(4000);
BEGIN
    UT_TRACE.TRACE('INICIA LDC_TRG_BIU_VALIDFLAG', 99);
    
    :New.EXONER_RECONN_CHARGE := 'N';
    
    UT_TRACE.TRACE('FIN LDC_TRG_BIU_VALIDFLAG', 99);
EXCEPTION
    WHEN EX.CONTROLLED_ERROR then
        ERRORS.GETERROR(nuError, sbError);
        UT_TRACE.TRACE('LDC_TRG_BIU_VALIDFLAG->Exc controlada: '||sbError, 99);
        RAISE EX.CONTROLLED_ERROR;
    WHEN OTHERS THEN
        ERRORS.SETERROR;
        ERRORS.GETERROR(nuError, sbError);
        UT_TRACE.TRACE('LDC_TRG_BIU_VALIDFLAG->Exc no controlada: '||sbError, 99);
        RAISE EX.CONTROLLED_ERROR;
END;
/