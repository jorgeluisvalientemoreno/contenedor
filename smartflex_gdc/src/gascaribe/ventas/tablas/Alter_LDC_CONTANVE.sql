DECLARE
 nuTab1 number := 0;
BEGIN
 
 SELECT COUNT(*) INTO nuTab1
 FROM Dba_Tab_Columns
 WHERE TABLE_NAME = 'LDC_CONTANVE' and  COLUMN_NAME =  'FECHREGI';

 if nuTab1 = 0 then
     execute IMMEDIATE 'ALTER TABLE LDC_CONTANVE ADD FECHREGI DATE DEFAULT SYSDATE';
	execute IMMEDIATE 'ALTER TABLE LDC_CONTANVE ADD FECHULMO DATE';
	execute IMMEDIATE 'ALTER TABLE LDC_CONTANVE ADD USUARIO VARCHAR2(400)';
	execute IMMEDIATE 'ALTER TABLE LDC_CONTANVE ADD TERMINAL VARCHAR2(400)';
	execute IMMEDIATE 'COMMENT ON COLUMN LDC_CONTANVE.FECHREGI IS ''FECHA DE REGISTRO''';
	execute IMMEDIATE 'COMMENT ON COLUMN LDC_CONTANVE.FECHULMO IS ''FECHA DE ULTIMA ACTUALIZACION''';
	execute IMMEDIATE 'COMMENT ON COLUMN LDC_CONTANVE.USUARIO IS ''USUARIO''';
	execute IMMEDIATE 'COMMENT ON COLUMN LDC_CONTANVE.TERMINAL IS ''TERMINAL''';	
  end if;

end;
/