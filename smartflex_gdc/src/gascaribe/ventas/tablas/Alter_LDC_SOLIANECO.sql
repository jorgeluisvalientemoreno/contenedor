DECLARE
 nuTab1 number := 0;
BEGIN
 
 SELECT COUNT(*) INTO nuTab1
 FROM Dba_Tab_Columns
 WHERE TABLE_NAME = 'LDC_SOLIANECO' and  COLUMN_NAME =  'OBSERVACION';

 if nuTab1 = 0 then
	execute IMMEDIATE 'CREATE INDEX IDX_CON03SOLIANECO ON LDC_SOLIANECO (DIRECCION, ESTADO)';
	execute IMMEDIATE 'ALTER TABLE LDC_SOLIANECO ADD OBSERVACION VARCHAR2(4000)';
	execute IMMEDIATE 'COMMENT ON COLUMN LDC_SOLIANECO.OBSERVACION IS ''OBSERVACION''';
	execute IMMEDIATE 'ALTER TABLE LDC_SOLIANECO ADD CONTRATO NUMBER(15)';
	execute IMMEDIATE 'COMMENT ON COLUMN LDC_SOLIANECO.CONTRATO IS ''CONTRATO''';
  end if;

end;
/