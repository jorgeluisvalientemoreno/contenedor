CREATE OR REPLACE FUNCTION "ADM_PERSON"."FNUGETITEMOSF_ROLLOUT" (coItem IN number, inubasedato number) RETURN NUMBER AS

   CURSOR CUITEMS  IS
   SELECT TO_NUMBER(ITEMCOSA)
   FROM   LDC_TEMP_ITEMS_SGE
   where  ITEMCODI = coItem;
   
   nuItem number;
   
BEGIN

  OPEN CUITEMS;
  FETCH CUITEMS INTO nuItem;
  if CUITEMS%NOTFOUND THEN
     nuItem := 0;
  END IF;
  CLOSE CUITEMS;

  RETURN NUITEM;
END FNUGETITEMOSF_ROLLOUT; 
/
BEGIN
    pkg_utilidades.prAplicarPermisos('FNUGETITEMOSF_ROLLOUT', 'ADM_PERSON');
END;
/
