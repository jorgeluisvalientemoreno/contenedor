SELECT q.quotation_id, Q.PACKAGE_ID, OO.Order_Id, oo.TASK_TYPE_ID, QI.UNIT_VALUE
  FROM CC_QUOTATION Q
  LEFT JOIN CC_QUOTATION_ITEM QI
    ON QI.QUOTATION_ID = Q.QUOTATION_ID
  LEFT JOIN MO_PACKAGES MP
    ON MP.PACKAGE_ID = Q.PACKAGE_ID
   AND MP.MOTIVE_STATUS_ID = 13
  LEFT JOIN OR_ORDER_ACTIVITY OOA
    ON OOA.PACKAGE_ID = MP.PACKAGE_ID
  LEFT JOIN OR_ORDER OO
    ON OO.ORDER_ID = OOA.ORDER_ID
   AND OO.Task_Type_Id in (12149, 12162, 12150, 10268)
 WHERE Q.STATUS IN ('A', 'C')
   AND QI.TASK_TYPE_ID in (12149, 12162, 12150, 10268)
   AND QI.REMAINING_ITEMS > 0
   and QI.UNIT_VALUE > 0
   and mp.request_date > '01/01/2024'
